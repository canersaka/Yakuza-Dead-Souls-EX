# Parallel lifting
#
# Translation is per-function and self-contained (each function's window of
# instructions fully determines its C body), so the main lift fans out to a
# process pool. Workers re-disassemble just their own byte ranges from the
# raw segment blobs, so the big instruction list never crosses the process
# boundary. Discovery and emission stay serial in the parent.
# ---------------------------------------------------------------------------

_WORKER_STATE: dict = {}


def _worker_init(segs, big_endian, name_map, trace=False):
    _WORKER_STATE["segs"] = segs
    _WORKER_STATE["be"] = big_endian
    _WORKER_STATE["names"] = name_map
    _WORKER_STATE["trace"] = trace


def _worker_lift(task):
    idx0, bounds = task
    lifter = PPULifter()
    lifter.name_map = _WORKER_STATE["names"]
    lifter.trace = _WORKER_STATE.get("trace", False)
    results = []
    for start, end in bounds:
        blob = b""
        for va, d in _WORKER_STATE["segs"]:
            if va <= start < va + len(d):
                blob = d[start - va:min(end - va, len(d))]
                break
        insns = disassemble_bytes(blob, start, _WORKER_STATE["be"]) if blob else []
        lifter._sorted_insns = None
        lifter._sorted_addrs = None
        f = lifter.lift_function(insns, start, end)
        results.append((f.name, f.start_addr, f.end_addr, f.body_lines, f.calls,
                        f.fallthrough_addr))
    return idx0, results, lifter.call_targets, lifter.branch_targets


def _parallel_lift(lifter, func_bounds, segs, big_endian, jobs):
    import multiprocessing as mp

    n = len(func_bounds)
    chunk = max(50, n // (jobs * 8))
    tasks = [(i, func_bounds[i:i + chunk]) for i in range(0, n, chunk)]
    print(f"  parallel lift: {jobs} workers, {len(tasks)} chunks of ~{chunk}", flush=True)

    results_by_idx = {}
    done = 0
    t0 = time.time()
    with mp.Pool(processes=jobs, initializer=_worker_init,
                 initargs=(segs, big_endian, lifter.name_map, lifter.trace)) as pool:
        for idx0, results, ct, bt in pool.imap_unordered(_worker_lift, tasks):
            results_by_idx[idx0] = results
            lifter.call_targets |= ct
            lifter.branch_targets |= bt
            done += len(results)
            elapsed = time.time() - t0
            eta = elapsed / done * (n - done) if done else 0
            print(f"  ... lifted {done}/{n} functions ({100 * done // n}%) "
                  f"elapsed {elapsed / 60:.1f}m eta {eta / 60:.1f}m", flush=True)

    for idx0 in sorted(results_by_idx):
        for name, s, e, body, calls, fallthrough in results_by_idx[idx0]:
            lifter.functions.append(LiftedFunction(
                name=name, start_addr=s, end_addr=e,
                body_lines=body, calls=calls, fallthrough_addr=fallthrough))


def main() -> None:
    parser = argparse.ArgumentParser(description="PPU to C lifter for PS3 recompilation")
    parser.add_argument("input", help="Input ELF or raw binary")
    parser.add_argument("--output", "-o", default=".", help="Output directory")
    parser.add_argument("--header-name", default="ppu_recomp.h", help="Header file name")
    parser.add_argument("--source-name", default="ppu_recomp.c", help="Source file name")
    parser.add_argument("--raw", action="store_true", help="Treat input as raw binary")
    parser.add_argument("--base", type=lambda x: int(x, 0), default=0,
                        help="Base address (for raw binary)")
    parser.add_argument("--little-endian", action="store_true")
    parser.add_argument("--trace", action="store_true",
                        help="Emit ppu_trace_pc(ctx, PC) before every instruction for the "
                             "PPU differential trace-diff (tools/tracediff.py). Produces a "
                             "trace-enabled build; keep it aside (bloats every function).")
    parser.add_argument("--functions", metavar="FILE",
                        help="JSON file with function list [{start, end}, ...]")
    parser.add_argument("--names", metavar="FILE", default=None,
                        help="JSON name map from ghidra_names.py "
                             "({\"0xADDR\": {\"label\": ...}}); emitted as comments")
    parser.add_argument("--jobs", "-j", type=int,
                        default=max(1, os.cpu_count() or 1),
                        help="Worker processes for the main lift "
                             "(default: CPU count; 1 = serial)")
    parser.add_argument("--extern-funcs", metavar="HEADER", default=None,
                        help="Header of another lifted object in the same link "
                             "(e.g. the game's ppu_recomp.h); its func_XXXXXXXX "
                             "declarations get no no-op stubs here, avoiding "
                             "duplicate-symbol link errors")
    parser.add_argument("--table-name", default="function_table",
                        help="Function-table symbol name (a second lifted "
                             "object in one link needs a non-default name)")
    args = parser.parse_args()

    with open(args.input, "rb") as f:
        file_data = f.read()

    big_endian = not args.little_endian
    base_addr = args.base

    # Get instructions (and keep the raw executable blobs for worker processes)
    seg_blobs: list[tuple[int, bytes]] = []
    if args.raw:
        all_insns = disassemble_bytes(file_data, base_addr, big_endian)
        seg_blobs = [(base_addr, file_data)]
    else:
        try:
            from elf_parser import ELFFile, PT_LOAD
            elf = ELFFile(args.input)
            elf.load()
            big_endian = elf.elf_header.big_endian
            all_insns = []
            for ph in elf.program_headers:
                if ph.p_type == PT_LOAD and (ph.p_flags & 1):
                    seg_data = elf.get_segment_data(elf.program_headers.index(ph))
                    all_insns.extend(disassemble_bytes(seg_data, ph.p_vaddr, big_endian))
                    seg_blobs.append((ph.p_vaddr, seg_data))
            if not all_insns:
                for ph in elf.program_headers:
                    if ph.p_type == PT_LOAD and ph.p_filesz > 0:
                        seg_data = elf.get_segment_data(elf.program_headers.index(ph))
                        all_insns.extend(disassemble_bytes(seg_data, ph.p_vaddr, big_endian))
                        seg_blobs.append((ph.p_vaddr, seg_data))
                        break
        except Exception as exc:
            print(f"Warning: ELF parse failed ({exc}), treating as raw", file=sys.stderr)
            all_insns = disassemble_bytes(file_data, base_addr, big_endian)
            seg_blobs = [(base_addr, file_data)]

    # Get function boundaries
    if args.functions:
        with open(args.functions) as f:
            func_list = json.load(f)
        func_bounds = [(int(str(e["start"]), 0), int(str(e["end"]), 0)) for e in func_list]
    else:
        detected = detect_functions(all_insns)
        func_bounds = [(f["start"], f["end"]) for f in detected]

    if not func_bounds:
        print("No functions found. Use --functions to provide a function list.", file=sys.stderr)
        sys.exit(1)

    # ----- jump-table (computed bctr) discovery ---------------------------
    # gcc switch dispatchers reach their case blocks via `mtctr; bctr` through
    # a data jump table — computed targets static analysis can't see, so the
    # cases never get lifted and the runtime indirect-call lands on an unlifted
    # address. Read the tables and add each case target as a SMALL standalone
    # function (bounded by the next case/function). Their `b <continuation>`
    # then references the continuation, which the mid-function pass lifts as a
    # tail-entry of whatever real function contains it. (We deliberately do NOT
    # extend the dispatcher function over the case block: the mid-entry tail
    # mechanism lifts target..func_end, so a far end explodes the output.)
    # Merged spans of the trusted function map: new entries (jump-table case
    # targets, discovery lifts) must start inside these, or they're data.
    code_spans: list[list[int]] = []
    for s, e in sorted(func_bounds):
        if code_spans and s <= code_spans[-1][1]:
            code_spans[-1][1] = max(code_spans[-1][1], e)
        else:
            code_spans.append([s, e])
    span_starts = [s for s, _ in code_spans]

    def _span_in_code(a: int) -> bool:
        i = bisect.bisect_right(span_starts, a) - 1
        return i >= 0 and a < code_spans[i][1]

    jt_targets = set()
    if not args.raw:
        try:
            seg_map = [(ph.p_vaddr, ph.p_vaddr + ph.p_filesz,
                        elf.get_segment_data(elf.program_headers.index(ph)))
                       for ph in elf.program_headers
                       if ph.p_type == PT_LOAD and ph.p_filesz > 0]
            def _read_u32(a):
                for v0, v1, d in seg_map:
                    if v0 <= a and a + 4 <= v1:
                        return int.from_bytes(d[a - v0:a - v0 + 4],
                                              'big' if big_endian else 'little')
                return None
            text_lo = min(s for s, _ in func_bounds)
            text_hi = max(e for _, e in func_bounds)
            toc = _read_u32((elf.elf_header.e_entry + 4) & 0xFFFFFFFF) or 0
            tables = discover_jump_tables(all_insns, _read_u32, toc, text_lo, text_hi)
            for ts in tables.values():
                jt_targets.update(ts)
            n_raw = len(jt_targets)
            # Case targets inside the trusted map are accepted outright. A
            # target in a MAP GAP can still be real code the scanner missed
            # (multi-epilogue functions get cut at an internal blr and their
            # switch cases land past the cut) — accept those iff a 16-word
            # decode probe at the target looks like code, mirroring the
            # discovery-pass plausibility probe.
            insn_addrs = sorted(i.addr for i in all_insns
                                if i.mnemonic != ".word")
            def _gap_target_plausible(a: int) -> bool:
                j = bisect.bisect_left(insn_addrs, a)
                if j >= len(insn_addrs) or insn_addrs[j] != a:
                    return False
                ok = 0
                for k in range(16):
                    if j + k < len(insn_addrs) and \
                       insn_addrs[j + k] == a + 4 * k:
                        ok += 1
                return ok >= 14
            in_span = {t for t in jt_targets if _span_in_code(t)}
            gap_ok = {t for t in jt_targets - in_span if _gap_target_plausible(t)}
            jt_targets = in_span | gap_ok
            fb = dict(func_bounds)
            allstarts = sorted(set(fb) | jt_targets)
            added = 0
            for t in sorted(jt_targets):
                if t in fb:
                    continue
                k = bisect.bisect_right(allstarts, t)
                end = allstarts[k] if k < len(allstarts) else text_hi
                i = bisect.bisect_right(span_starts, t) - 1
                if i >= 0 and t < code_spans[i][1]:
                    end = min(end, code_spans[i][1])
                else:
                    # gap target: no containing span to clamp to; cap at the
                    # discovery pass's 16 KB distance bound instead
                    end = min(end, t + 0x4000)
                fb[t] = end
                added += 1
            func_bounds = sorted(fb.items())
            print(f"  jump tables: {len(tables)} dispatchers, {len(jt_targets)} case targets "
                  f"({len(gap_ok)} gap-rescued, {n_raw - len(jt_targets)} rejected as data), "
                  f"+{added} case funcs")
        except Exception as exc:
            print(f"  jump-table discovery skipped: {exc}", file=sys.stderr)

    print(f"Lifting {len(func_bounds)} functions...")

    lifter = PPULifter()
    lifter.set_code_ranges(func_bounds)
    lifter.table_name = args.table_name
    lifter.header_name = args.header_name
    lifter.trace = args.trace
    if args.extern_funcs:
        decl_pat = re.compile(r"^void func_([0-9A-Fa-f]{8})\(ppu_context\* ctx\);",
                              re.M)
        with open(args.extern_funcs) as ef:
            lifter.extern_funcs = {int(a, 16) for a in decl_pat.findall(ef.read())}
        print(f"  extern-funcs: {len(lifter.extern_funcs)} symbols from "
              f"{args.extern_funcs} excluded from stub emission", flush=True)

    # Optional: load a recovered-name map (from Ghidra analysis) to annotate
    # generated functions with meaningful names as comments.
    if args.names:
        import json as _json
        with open(args.names) as nf:
            nm = _json.load(nf)
        loaded = 0
        for k, v in nm.items():
            try:
                addr = int(str(k), 16)
            except ValueError:
                continue
            label = v.get("label") if isinstance(v, dict) else str(v)
            if label:
                lifter.name_map[addr] = label
                loaded += 1
        print(f"  Loaded {loaded} recovered names from {args.names}")

    _n_funcs = len(func_bounds)
    if args.jobs > 1:
        _parallel_lift(lifter, func_bounds, seg_blobs, big_endian, args.jobs)
    else:
        _t0 = time.time()
        for _i, (start, end) in enumerate(func_bounds):
            lifter.lift_function(all_insns, start, end)
            if (_i + 1) % 1000 == 0 or _i == _n_funcs - 1:
                _el = time.time() - _t0
                _eta = _el / (_i + 1) * (_n_funcs - _i - 1)
                print(f"  ... lifted {_i + 1}/{_n_funcs} functions "
                      f"elapsed {_el / 60:.1f}m eta {_eta / 60:.1f}m", flush=True)

    # Resolve mid-function entry points: generate tail-entry functions for
    # branch/trampoline targets that land inside existing function bodies.
    # This runs iteratively because newly generated tail-entry functions may
    # themselves contain branches to other mid-function addresses.
    total_mid = 0
    max_passes = 30  # generalized discovery (incl. missed functions) converges transitively
    print(f"Main lift done ({len(lifter.functions)} functions). Now discovering functions "
          f"reachable from the lifted code (calls/branches not in functions.json)...", flush=True)
    for pass_num in range(1, max_passes + 1):
        print(f"  -- discovery pass {pass_num} --", flush=True)
        n = lifter.generate_mid_function_entries(all_insns)
        if n == 0:
            print(f"  discovery converged after pass {pass_num} (nothing new).", flush=True)
            break
        total_mid += n
        print(f"  pass {pass_num}: +{n} functions ({total_mid} discovered, "
              f"{len(lifter.functions)} total)", flush=True)
    if total_mid:
        print(f"  Discovery done: +{total_mid} functions ({len(lifter.functions)} total).", flush=True)

    os.makedirs(args.output, exist_ok=True)

    header_path = os.path.join(args.output, args.header_name)
    with open(header_path, "w") as f:
        f.write(lifter.emit_header())

    # Remove stale output from older lifter versions (the single-file .c and
    # any previous chunk set) so the build doesn't pick up both.
    import glob as _glob
    base = args.source_name[:-2] if args.source_name.endswith(".c") else args.source_name
    for stale in ([os.path.join(args.output, args.source_name)] +
                  _glob.glob(os.path.join(args.output, f"{base}_*.c")) +
                  _glob.glob(os.path.join(args.output, f"{base}_*.cpp"))):
        if os.path.exists(stale):
            os.remove(stale)

    print("Writing C source (split into chunks)...", flush=True)
    paths = lifter.write_source_files(args.output, base=base)

    word_todos = 0
    insn_todos = 0
    for fn in lifter.functions:
        for ln in fn.body_lines:
            if "/* TODO" in ln:
                if ".word" in ln:
                    word_todos += 1
                else:
                    insn_todos += 1

    print(f"Wrote {header_path}")
    print(f"Wrote {len(paths)} source chunks: "
          f"{os.path.basename(paths[0])} .. {os.path.basename(paths[-1])}")
    print(f"  {len(lifter.functions)} functions lifted")
    print(f"  {len(lifter.call_targets)} unique call targets")
    print(f"  TODOs: {insn_todos} unhandled instructions (real gaps), "
          f"{word_todos} .word lines (data in text, harmless)")


if __name__ == "__main__":
    main()
