    # Code-coverage contract
    # ------------------------------------------------------------------ #

    def set_code_ranges(self, bounds) -> None:
        """Record the merged [start, end) spans of the trusted function map.
        Any NEW entry the lifter invents afterwards (jump-table case targets,
        discovery lifts) must start inside these spans. Executable segments
        also contain read-only data; a junk target pointing into data used to
        get lifted with `end = next known function start`, which can be
        megabytes away across that data -- producing megabytes of garbage
        code per junk target. Outside-coverage targets become no-op stubs."""
        merged: list[list[int]] = []
        for s, e in sorted(bounds):
            if merged and s <= merged[-1][1]:
                merged[-1][1] = max(merged[-1][1], e)
            else:
                merged.append([s, e])
        self.code_ranges = merged
        self._code_starts = [m[0] for m in merged]

    def _in_code(self, addr: int) -> bool:
        if getattr(self, "code_ranges", None) is None:
            return True
        i = bisect.bisect_right(self._code_starts, addr) - 1
        return i >= 0 and addr < self.code_ranges[i][1]

    def _code_range_end(self, addr: int) -> int:
        i = bisect.bisect_right(self._code_starts, addr) - 1
        if i >= 0 and addr < self.code_ranges[i][1]:
            return self.code_ranges[i][1]
        return addr

    # ------------------------------------------------------------------ #
    # Mid-function entry point resolution
    # ------------------------------------------------------------------ #

    def generate_mid_function_entries(self, all_insns: list[Instruction]) -> int:
        """Generate tail-entry functions for branch targets that land inside
        existing function bodies.

        When a trampoline or call references ``func_X`` and address X falls
        within [start, end) of an already-lifted function F, X is a
        mid-function entry point.  We generate a new function ``func_X``
        containing the lifted code from address X to F's end.

        Returns the number of tail-entry functions generated.
        """
        defined = {f.start_addr for f in self.functions}
        # All addresses that are referenced as func_X but not yet defined
        all_refs = (self.call_targets | self.branch_targets) - defined

        if not all_refs:
            return 0

        # Build an interval map: sorted list of (start, end) for existing funcs
        func_intervals = sorted((f.start_addr, f.end_addr) for f in self.functions)
        start_list = [s for s, _ in func_intervals]

        # Set of real instruction addresses, so we only lift code (not data /
        # bogus targets). Cached — the instruction stream doesn't change.
        if self._valid_addrs is None:
            self._valid_addrs = {insn.addr for insn in all_insns}
        valid_addrs = self._valid_addrs

        # The sorted-instruction cache backs the code-plausibility probe
        # below; make sure it exists even if no lift_function call built it
        # yet (the parallel path reaches discovery with a fresh cache).
        if self._sorted_insns is None:
            self._sorted_insns = sorted(all_insns, key=lambda i: i.addr)
            self._sorted_addrs = [i.addr for i in self._sorted_insns]

        n_refs = len(all_refs)
        print(f"    discovery: {n_refs} referenced targets to check...", flush=True)
        count = 0
        for idx, target in enumerate(sorted(all_refs)):
            if idx and idx % 500 == 0:
                print(f"      ... {idx}/{n_refs} checked, {count} new functions lifted",
                      flush=True)
            # Binary search: find the function whose range contains target
            container = None
            lo, hi = 0, len(func_intervals) - 1
            while lo <= hi:
                mid = (lo + hi) // 2
                fstart, fend = func_intervals[mid]
                if target < fstart:
                    hi = mid - 1
                elif target >= fend:
                    lo = mid + 1
                else:
                    # fstart <= target < fend, but skip if target == fstart
                    # (that's the function itself, already defined)
                    if target != fstart:
                        container = (fstart, fend)
                    break

            if container is None:
                # Target is outside every known function: either a function
                # the boundary detection missed, or junk pointing into data.
                # Tell them apart by the distance to the next known function:
                # a real missed function is hemmed in by neighbors (small
                # bound), while a junk target in a read-only data region has
                # no neighbors for a very long way -- lifting those used to
                # produce megabytes of garbage code per target. Inside the
                # trusted code coverage, lift bounded by the span; outside,
                # lift only if the bound is tight; otherwise emit a no-op
                # stub.
                if target in valid_addrs:
                    ni = bisect.bisect_right(start_list, target)
                    end = start_list[ni] if ni < len(start_list) else target + 0x1000
                    if self._in_code(target):
                        end = min(end, self._code_range_end(target))
                    else:
                        # Outside the trusted map: real missed functions are
                        # hemmed in by neighbors AND decode cleanly; junk
                        # targets in data regions fail one or both. Without
                        # this, garbage lifts reference more junk targets and
                        # the cascade produces megabytes of nonsense code.
                        if end - target > 0x4000:
                            continue   # data desert -> no-op stub at emit time
                        lo = bisect.bisect_left(self._sorted_addrs, target)
                        probe = self._sorted_insns[lo:lo + 16]
                        # A switch dispatcher is a short code block
                        # (lwz/lwzx/add/mtctr; bctr) immediately followed by its
                        # jump-TABLE DATA, which decodes as `.word`. Such a target
                        # is real code, so don't reject it just because `.word`
                        # appears within 16 insns: accept iff the run up to the
                        # first `.word` ends in an unconditional control transfer
                        # (b/bctr/blr/blrl). Junk data targets instead hit `.word`
                        # with no preceding terminator.
                        _TERMS = ("b", "bctr", "blr", "blrl", "bctrl")
                        if not probe or probe[0].addr != target:
                            continue   # nothing decodes here -> stub
                        saw_term = False
                        for _p in probe:
                            if _p.mnemonic == ".word":
                                break
                            if _p.mnemonic in _TERMS:
                                saw_term = True
                                break
                        if not saw_term and any(p.mnemonic == ".word"
                                                for p in probe):
                            continue   # data, not code -> stub
                    if end > target:
                        self.lift_function(all_insns, target, end)
                        defined.add(target)
                        count += 1
                continue

            fstart, fend = container
            # Lift the tail: from target to fend (mid-function entry point)
            tail_func = self.lift_function(all_insns, target, fend)
            # lift_function already appends to self.functions, so it's included
            # in output. Update defined set so we don't re-generate.
            defined.add(target)
            count += 1

        return count

    # ------------------------------------------------------------------ #
    # Output generation
    # ------------------------------------------------------------------ #

    def emit_header(self) -> str:
        """Generate the C header file content."""
        lines = [HEADER_PREAMBLE]
        if self.table_name != "function_table":
            lines.append('#ifdef __cplusplus\nextern "C" {\n#endif')
            lines.append(f"extern const func_entry {self.table_name}[];")
            lines.append(f"extern const uint64_t  {self.table_name}_count;")
            lines.append('#ifdef __cplusplus\n}\n#endif')
            lines.append("")
        # Forward declarations
        for func in self.functions:
            lines.append(f"void {func.name}(ppu_context* ctx);")
        # Also declare any referenced targets (calls AND branch/trampoline
        # targets) that aren't defined, so the source always compiles.
        defined = {f.start_addr for f in self.functions}
        for target in sorted((self.call_targets | self.branch_targets) - defined):
            lines.append(f"void func_{target:08X}(ppu_context* ctx); /* external */")
        lines.append("")
        return "\n".join(lines)

    def _preamble_lines(self) -> list[str]:
        """Source preamble + static-inline helper functions, emitted into
        every chunk file (the helpers are `static inline`, so duplicating
        them across translation units is harmless and keeps each chunk
        self-contained)."""
        lines = [SOURCE_PREAMBLE.replace("{header_name}", self.header_name)]

        # --trace: the runtime provides ppu_trace_pc (defined in the game project,
        # extern "C"). ctx is passed but ignored (PC-only trace uses the PC arg +
        # the current thread id), so a void* param avoids coupling to ppu_context.
        if self.trace:
            lines.append('extern "C" void ppu_trace_pc(void* ctx, uint32_t pc);')

        # Emit helper macros
        lines.append("/* Rotate helpers */")
        lines.append("static inline uint32_t ppc_rlwinm(uint32_t rs, int sh, int mb, int me) {")
        lines.append("    uint32_t rotated = sh ? ((rs << sh) | (rs >> (32 - sh))) : rs;")
        lines.append("    uint32_t mask;")
        lines.append("    if (mb <= me) {")
        lines.append("        mask = ((uint32_t)-1 >> mb) & ((uint32_t)-1 << (31 - me));")
        lines.append("    } else {")
        lines.append("        mask = ((uint32_t)-1 >> mb) | ((uint32_t)-1 << (31 - me));")
        lines.append("    }")
        lines.append("    return rotated & mask;")
        lines.append("}")
        lines.append("")
        lines.append("static inline uint32_t ppc_rlwimi(uint32_t ra, uint32_t rs, int sh, int mb, int me) {")
        lines.append("    uint32_t rotated = sh ? ((rs << sh) | (rs >> (32 - sh))) : rs;")
        lines.append("    uint32_t mask;")
        lines.append("    if (mb <= me) {")
        lines.append("        mask = ((uint32_t)-1 >> mb) & ((uint32_t)-1 << (31 - me));")
        lines.append("    } else {")
        lines.append("        mask = ((uint32_t)-1 >> mb) | ((uint32_t)-1 << (31 - me));")
        lines.append("    }")
        lines.append("    return (rotated & mask) | (ra & ~mask);")
        lines.append("}")
        lines.append("")
        # 64-bit rotate helpers
        lines.append("/* 64-bit rotate helpers */")
        lines.append("static inline uint64_t ppc_rotl64(uint64_t v, int n) {")
        lines.append("    n &= 63;")
        lines.append("    return n ? ((v << n) | (v >> (64 - n))) : v;")
        lines.append("}")
        lines.append("static inline uint64_t ppc_mask64(int mb, int me) {")
        lines.append("    uint64_t mask;")
        lines.append("    if (mb <= me) {")
        lines.append("        mask = (mb == 0) ? ~0ULL : (~0ULL >> mb);")
        lines.append("        mask &= (me == 63) ? ~0ULL : (~0ULL << (63 - me));")
        lines.append("    } else {")
        lines.append("        mask = (~0ULL >> mb) | (~0ULL << (63 - me));")
        lines.append("    }")
        lines.append("    return mask;")
        lines.append("}")
        lines.append("static inline uint64_t ppc_rldicl(uint64_t rs, int sh, int mb) {")
        lines.append("    return ppc_rotl64(rs, sh) & ppc_mask64(mb, 63);")
        lines.append("}")
        lines.append("static inline uint64_t ppc_rldicr(uint64_t rs, int sh, int me) {")
        lines.append("    return ppc_rotl64(rs, sh) & ppc_mask64(0, me);")
        lines.append("}")
        lines.append("static inline uint64_t ppc_rldic(uint64_t rs, int sh, int mb) {")
        lines.append("    int me = 63 - sh;")
        lines.append("    return ppc_rotl64(rs, sh) & ppc_mask64(mb, me);")
        lines.append("}")
        lines.append("static inline uint64_t ppc_rldimi(uint64_t ra, uint64_t rs, int sh, int mb) {")
        lines.append("    int me = 63 - sh;")
        lines.append("    uint64_t mask = ppc_mask64(mb, me);")
        lines.append("    return (ppc_rotl64(rs, sh) & mask) | (ra & ~mask);")
        lines.append("}")
        lines.append("")
        # Shift-right-algebraic with XER[CA]. CA = rS<0 AND any 1-bits shifted out.
        # Also clamps shift counts >= width (PPC yields all-sign; a raw C shift by
        # >= the width is undefined). The carry feeds adde/addze/subfe consumers.
        lines.append("static inline int64_t ppc_sraw(uint32_t* xer, int32_t rs, int n) {")
        lines.append("    int sh = n & 0x3F;")
        lines.append("    uint32_t so = (sh == 0) ? 0u : ((sh >= 32) ? (uint32_t)rs : ((uint32_t)rs & (((uint32_t)1u << sh) - 1u)));")
        lines.append("    uint32_t ca = (rs < 0 && so != 0u) ? 1u : 0u;")
        lines.append("    *xer = (*xer & ~(1u << 29)) | (ca << 29);")
        lines.append("    return (int64_t)((sh >= 32) ? (rs >> 31) : (rs >> sh));")
        lines.append("}")
        lines.append("static inline int64_t ppc_srad(uint32_t* xer, int64_t rs, int n) {")
        lines.append("    int sh = n & 0x7F;")
        lines.append("    uint64_t so = (sh == 0) ? 0ull : ((sh >= 64) ? (uint64_t)rs : ((uint64_t)rs & (((uint64_t)1ull << sh) - 1ull)));")
        lines.append("    uint32_t ca = (rs < 0 && so != 0ull) ? 1u : 0u;")
        lines.append("    *xer = (*xer & ~(1u << 29)) | (ca << 29);")
        lines.append("    return (sh >= 64) ? (rs >> 63) : (rs >> sh);")
        lines.append("}")
        lines.append("")
        # ---- VMX lane-endianness accessors ----
        # ctx->vr holds RAW big-endian bytes (lvx/stvx do a plain memcpy: guest
        # memory byte 0 == vr.b[0], the MSB, matching AltiVec PEM Fig. 1-3 and
        # 1.3.2.1: an aligned quadword load places EA's byte into byte element 0).
        # BE element k of a typed view therefore lives at STORAGE INDEX k, but a
        # little-endian host reads the multi-byte value byte-reversed. These
        # accessors read/write element k (index k) with the per-width byte swap,
        # so a handler's lane math sees the correct BE-ordered scalar value while
        # byte-granular ops (vperm/lvsl/vsldoi/lvlx/merge/pack) keep using vr.b[]
        # untouched. Bytes need no swap. Convention: "element k at index k".
        lines.append("/* VMX big-endian lane accessors (raw-BE storage; element k at index k) */")
        lines.append("#if defined(_MSC_VER)")
        lines.append("#define VR_BSWAP16(x) _byteswap_ushort(x)")
        lines.append("#define VR_BSWAP32(x) _byteswap_ulong(x)")
        lines.append("#define VR_BSWAP64(x) _byteswap_uint64(x)")
        lines.append("#elif defined(__GNUC__) || defined(__clang__)")
        lines.append("#define VR_BSWAP16(x) __builtin_bswap16(x)")
        lines.append("#define VR_BSWAP32(x) __builtin_bswap32(x)")
        lines.append("#define VR_BSWAP64(x) __builtin_bswap64(x)")
        lines.append("#else")
        lines.append("static inline uint16_t VR_BSWAP16(uint16_t v){return (uint16_t)((v>>8)|(v<<8));}")
        lines.append("static inline uint32_t VR_BSWAP32(uint32_t v){v=((v&0x00FF00FFu)<<8)|((v&0xFF00FF00u)>>8);return (v<<16)|(v>>16);}")
        lines.append("static inline uint64_t VR_BSWAP64(uint64_t v){v=((v&0x00FF00FF00FF00FFull)<<8)|((v&0xFF00FF00FF00FF00ull)>>8);v=((v&0x0000FFFF0000FFFFull)<<16)|((v&0xFFFF0000FFFF0000ull)>>16);return (v<<32)|(v>>32);}")
        lines.append("#endif")
        # Accessors take a raw pointer to the 16-byte lane storage (void*), so
        # they are agnostic to whether ctx->vr is typed ppu_vr (generated header)
        # or u128 (runtime ppu_context.h) -- both are the same 16 raw BE bytes.
        # BE element k of width W lives at byte offset k*W (= storage index k).
        lines.append("static inline uint16_t vrh(const void* v, int i) { uint16_t r; memcpy(&r,(const uint8_t*)v+i*2,2); return VR_BSWAP16(r); }")
        lines.append("static inline uint32_t vrw(const void* v, int i) { uint32_t r; memcpy(&r,(const uint8_t*)v+i*4,4); return VR_BSWAP32(r); }")
        lines.append("static inline uint64_t vrd(const void* v, int i) { uint64_t r; memcpy(&r,(const uint8_t*)v+i*8,8); return VR_BSWAP64(r); }")
        lines.append("static inline void vsth(void* v, int i, uint16_t x) { uint16_t r=VR_BSWAP16(x); memcpy((uint8_t*)v+i*2,&r,2); }")
        lines.append("static inline void vstw(void* v, int i, uint32_t x) { uint32_t r=VR_BSWAP32(x); memcpy((uint8_t*)v+i*4,&r,4); }")
        lines.append("static inline void vstd(void* v, int i, uint64_t x) { uint64_t r=VR_BSWAP64(x); memcpy((uint8_t*)v+i*8,&r,8); }")
        lines.append("static inline float vrf(const void* v, int i) { uint32_t u=vrw(v,i); float f; memcpy(&f,&u,4); return f; }")
        lines.append("static inline void vstf(void* v, int i, float f) { uint32_t u; memcpy(&u,&f,4); vstw(v,i,u); }")
        lines.append("")
        return lines

    def _function_def_lines(self, func) -> list[str]:
        """C lines for one lifted function (body + fallthrough trampoline)."""
        lines: list[str] = []
        label = self.name_map.get(func.start_addr)
        if label:
            lines.append(f"/* {label} */")
        lines.append(f"void {func.name}(ppu_context* ctx) {{")
        for bline in func.body_lines:
            lines.append(f"    {bline}" if not bline.endswith(":") else bline)

        # If the function's last instruction isn't an unconditional control
        # transfer, PPC execution falls through to end_addr (decided at lift
        # time, see lift_function). Chain via the trampoline to avoid deep
        # host call stacks.
        if func.fallthrough_addr:
            lines.append(f"        {{ g_trampoline_fn = (void(*)(void*))"
                         f"func_{func.fallthrough_addr:08X}; return; }}")

        lines.append("}")
        lines.append("")
        return lines

    def _stub_and_table_lines(self) -> list[str]:
        """No-op stubs for unresolved targets + the exported function table.
        Emitted once, into the final chunk file."""
        lines: list[str] = []
        # No-op stub bodies for referenced targets that could not be lifted
        # (bogus/unresolved branch or call targets). Declared /* external */ in
        # the header; a mis-resolved branch becomes a harmless no-op rather than
        # a link error or crash.
        defined_addrs = {f.start_addr for f in self.functions}
        for target in sorted((self.call_targets | self.branch_targets)
                             - defined_addrs - self.extern_funcs):
            lines.append(
                f"void func_{target:08X}(ppu_context* ctx) {{ (void)ctx; "
                f"/* unresolved target 0x{target:08X} */ }}")
        lines.append("")
        # Function table (typedef + extern decl live in the header; external
        # linkage so the game project's dispatcher can use it).
        lines.append("/* Function table */")
        lines.append(f"const func_entry {self.table_name}[] = {{")
        for func in self.functions:
            lines.append(f'    {{ 0x{func.start_addr:08X}ULL, {func.name}, "{func.name}" }},')
        lines.append("    { 0, NULL, NULL }")
        lines.append("};")
        lines.append(f"const uint64_t {self.table_name}_count = {len(self.functions)};")
        lines.append("")
        return lines

    def emit_source(self) -> str:
        """Full single-file C source (kept for callers/tests; the real build
        uses write_source_files, which splits to stay under the MSVC source
        line limit)."""
        lines = self._preamble_lines()
        for func in self.functions:
            lines += self._function_def_lines(func)
        lines += self._stub_and_table_lines()
        return "\n".join(lines)

    def write_source_files(self, out_dir: str, base: str = "ppu_recomp",
                           ext: str = ".cpp", max_lines: int = 600_000) -> list[str]:
        """Write the C source split across chunk files, each well under the
        MSVC 16,777,215 source-line limit. Splitting also lets the build
        compile chunks in parallel and rebuild incrementally. Returns the
        list of written paths. Chunks are cut only at function boundaries."""
        preamble = self._preamble_lines()
        written: list[str] = []
        chunk_idx = 0

        def flush(body_lines: list[str], trailer: list[str] | None = None) -> None:
            nonlocal chunk_idx
            path = os.path.join(out_dir, f"{base}_{chunk_idx:03d}{ext}")
            with open(path, "w") as f:
                f.write("\n".join(preamble + body_lines + (trailer or [])))
            written.append(path)
            print(f"  wrote {os.path.basename(path)} "
                  f"({len(preamble) + len(body_lines) + len(trailer or [])} lines)",
                  flush=True)
            chunk_idx += 1

        cur: list[str] = []
        cur_len = len(preamble)
        n = len(self.functions)
        for i, func in enumerate(self.functions):
            if i % 10000 == 0:
                print(f"  ... emitting {i}/{n} functions", flush=True)
            deflines = self._function_def_lines(func)
            if cur and cur_len + len(deflines) > max_lines:
                flush(cur)
                cur = []
                cur_len = len(preamble)
            cur += deflines
            cur_len += len(deflines)

        # Final chunk carries the stubs + function table.
        flush(cur, self._stub_and_table_lines())
        return written


