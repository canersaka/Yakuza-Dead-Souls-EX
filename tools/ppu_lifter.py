# ---------------------------------------------------------------------------
# Instruction -> C translation
# ---------------------------------------------------------------------------

def _parse_operands(operands: str) -> list[str]:
    """Split operands string by commas, stripping whitespace."""
    return [o.strip() for o in operands.split(",") if o.strip()]


def _reg_idx(token: str) -> str:
    """Extract register index from 'r3' -> '3', 'f5' -> '5', 'v2' -> '2'."""
    m = re.match(r"[rfv](\d+)", token)
    return m.group(1) if m else token


def _disp_base(token: str):
    """Parse 'disp(rN)' -> (disp_str, N)."""
    m = re.match(r"(-?0x[0-9A-Fa-f]+|[0-9-]+)\(r(\d+)\)", token)
    if m:
        disp = m.group(1)
        base = m.group(2)
        return disp, base
    return None, None


def _imm(token: str) -> str:
    """Return a C literal for an immediate value token."""
    token = token.strip()
    if token.startswith("0x") or token.startswith("-0x"):
        return token
    try:
        v = int(token)
        if v < 0:
            return str(v)
        return f"0x{v:X}" if v > 9 else str(v)
    except ValueError:
        return token


def _cr_field_set(bf: int) -> str:
    """Generate C to set a CR field from a comparison result."""
    shift = (7 - bf) * 4
    return (f"ctx->cr = (ctx->cr & ~(0xFu << {shift})) | "
            f"(cr_val << {shift})")


@dataclass
class LiftedFunction:
    """A single lifted C function."""
    name: str = ""
    start_addr: int = 0
    end_addr: int = 0
    body_lines: list[str] = field(default_factory=list)
    calls: list[int] = field(default_factory=list)  # addresses of bl targets
    fallthrough_addr: int = 0  # execution continues here past end_addr (0 = none)


class PPULifter:
    """Translates PPU instructions into C source."""

    def __init__(self):
        self.functions: list[LiftedFunction] = []
        self.call_targets: set[int] = set()
        self.branch_targets: set[int] = set()  # all func_X references (b/bc trampolines)
        self._valid_addrs: set[int] | None = None  # cache: addrs of real instructions
        self._sorted_insns: list | None = None       # instructions sorted by addr (index)
        self._sorted_addrs: list[int] | None = None  # parallel addr list for bisect
        # addr(int) -> recovered name label (from Ghidra analysis). Emitted as a
        # comment above func_ADDR so dispatch stays address-based.
        self.name_map: dict[int, str] = {}
        # Targets defined by ANOTHER lifted object linked into the same binary
        # (e.g. the game lift when lifting a firmware PRX). These get an extern
        # declaration but no no-op stub, so the linker binds the real function
        # instead of erroring on a duplicate definition.
        self.extern_funcs: set[int] = set()
        # Trace mode (--trace): emit ppu_trace_pc(ctx, PC) before every instruction
        # for the PPU differential trace-diff (tools/tracediff.py). Off by default;
        # a trace-enabled build is kept aside (the emission bloats every function).
        self.trace: bool = False
        # Function-table symbol name; a second lifted object in the same link
        # must use a different name than the default.
        self.table_name: str = "function_table"
        # Header file name the source chunks #include (matches --header-name).
        self.header_name: str = "ppu_recomp.h"

    def lift_function(self, instructions: list[Instruction],
                      start: int, end: int) -> LiftedFunction:
        """Lift a range of instructions into a C function."""
        func = LiftedFunction(
            name=f"func_{start:08X}",
            start_addr=start,
            end_addr=end,
        )

        # Address index, built once: instructions sorted by address + a parallel
        # address list. We grab this function's instruction slice by binary
        # search instead of scanning the entire instruction list on every call.
        # This turns lifting from O(functions x total_instructions) into ~O(total).
        if self._sorted_insns is None:
            self._sorted_insns = sorted(instructions, key=lambda i: i.addr)
            self._sorted_addrs = [i.addr for i in self._sorted_insns]
        lo = bisect.bisect_left(self._sorted_addrs, start)
        hi = bisect.bisect_left(self._sorted_addrs, end)
        window = self._sorted_insns[lo:hi]   # exactly the instrs with start <= addr < end

        # Collect branch targets within the function for labels
        internal_targets: set[int] = set()
        for insn in window:
            if insn.mnemonic.startswith("b") and insn.mnemonic not in (
                    "blr", "bctr", "bctrl", "bl", "blrl"):
                ops = _parse_operands(insn.operands)
                for op in ops:
                    if op.startswith("0x"):
                        try:
                            target = int(op, 16)
                            if start <= target < end:
                                internal_targets.add(target)
                        except ValueError:
                            pass

        for insn in window:
            # Label
            if insn.addr in internal_targets:
                func.body_lines.append(f"loc_{insn.addr:08X}:")

            # --trace: log the PC about to execute (control-flow trace for the
            # PPU differential trace-diff). Emitted AFTER the label so a branch
            # target's PC is logged when reached. PC-only for now (compute-value
            # trace = ppu_trace_rt is a later phase).
            if self.trace:
                func.body_lines.append(f"    ppu_trace_pc(ctx, 0x{insn.addr:X});")

            c_line = self._translate(insn, func)
            if c_line:
                func.body_lines.append(f"    {c_line}")

        # PPC execution falls through past end_addr unless the last
        # instruction is an unconditional control transfer (the mnemonics
        # whose translation never continues: blr/blrl -> return, b -> goto or
        # trampoline, bctr -> indirect dispatch + return). The continuation
        # is at end_addr itself -- NOT the next function in the map, which
        # may sit past an unmapped gap. Register it as a branch target so
        # gap continuations get lifted by the discovery pass.
        if window and window[-1].mnemonic not in ("b", "blr", "blrl", "bctr"):
            func.fallthrough_addr = end
            self.branch_targets.add(end)

        self.functions.append(func)
        return func

    # ------------------------------------------------------------------ #
    # Per-instruction translation
    # ------------------------------------------------------------------ #

    # Record-form (Rc=1) integer ops also set CR0 from a signed compare of
    # the result against zero, plus XER[SO]. The op handlers produce the
    # arithmetic; the _translate wrapper appends the CR0 update for any
    # dot-form whose first operand is the destination GPR. Excluded:
    # stwcx./stdcx. (their handlers set CR0 themselves), FP dot-forms
    # (those set CR1), VMX dot-forms (those set CR6), mt*/mf* specials.
    _CR0_SELF_HANDLED = ("stwcx.", "stdcx.")

    def _translate(self, insn: Instruction, func: LiftedFunction) -> str:
        code = self._translate_op(insn, func)
        mn = insn.mnemonic
        if (mn.endswith(".") and code and not code.startswith("/*")
                and mn not in self._CR0_SELF_HANDLED
                and not mn.startswith(("f", "v", "mt", "mf"))):
            ops = _parse_operands(insn.operands)
            m = re.match(r"r(\d+)$", ops[0]) if ops else None
            if m:
                code += (f" {{ int64_t _r = (int64_t)ctx->gpr[{m.group(1)}]; "
                         f"uint32_t _c = (_r < 0) ? 8u : (_r > 0) ? 4u : 2u; "
                         f"_c |= (uint32_t)((ctx->xer >> 31) & 1u); "
                         f"ctx->cr = (ctx->cr & 0x0FFFFFFFu) | (_c << 28); }}")
        return code

    def _translate_op(self, insn: Instruction, func: LiftedFunction) -> str:
        """Translate one Instruction to a C statement string."""
        mn = insn.mnemonic
        ops = _parse_operands(insn.operands)
        addr = insn.addr

        # ------- Arithmetic -------
        if mn == "li":
            rd = _reg_idx(ops[0])
            return f"ctx->gpr[{rd}] = (int64_t)(int32_t)({_imm(ops[1])});"

        if mn == "lis":
            rd = _reg_idx(ops[0])
            return f"ctx->gpr[{rd}] = (int64_t)(int32_t)((uint32_t){_imm(ops[1])} << 16);"

        if mn == "addi":
            # Full 64-bit add (same reasoning as `add`: the 32-bit truncation
            # breaks genuine 64-bit arithmetic; conformance suite 2026-07-02).
            # EAs stay identical (guest addresses are < 2^31, and the memory
            # helpers mask to 32 bits anyway).
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{rd}] = ctx->gpr[{ra}] + (uint64_t)(int64_t){_imm(ops[2])};"

        if mn == "addis":
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"ctx->gpr[{rd}] = ctx->gpr[{ra}] + "
                    f"(uint64_t)(int64_t)(int32_t)((uint32_t){_imm(ops[2])} << 16);")

        if mn == "addic" or mn == "addic.":
            # Full 64-bit add (same reasoning as `add` below) AND record XER[CA]
            # -- addic/subfic are the D-form members of the carry family; they
            # NEVER set CA before (conformance suite 2026-07-02), so any
            # adde/subfe consuming their carry computed garbage.
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"{{ uint64_t _s = ctx->gpr[{ra}] + (uint64_t)(int64_t){_imm(ops[2])}; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"((_s < ctx->gpr[{ra}]) ? (1u << 29) : 0); "
                    f"ctx->gpr[{rd}] = _s; }}")

        if mn in ("add", "add.", "addo", "addo."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # PPC `add` is a full 64-bit add. Truncating to 32 bits breaks
            # genuine 64-bit arithmetic such as the SWAR word-at-a-time strlen
            # (`add rX, masked_word, 0x7F7F7F7F7F7F7F7F`), which then never
            # detects the terminator byte in the high word and scans forever.
            return f"ctx->gpr[{rd}] = ctx->gpr[{ra}] + ctx->gpr[{rb}];"

        if mn in ("subf", "subf.", "subfo", "subfo."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{rd}] = ctx->gpr[{rb}] - ctx->gpr[{ra}];"

        if mn == "subfic":
            # 64-bit ~RA + imm + 1 with XER[CA] (PowerISA; CA was never set).
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"{{ uint64_t _na = ~ctx->gpr[{ra}]; "
                    f"uint64_t _t = _na + (uint64_t)(int64_t){_imm(ops[2])}; "
                    f"uint64_t _s = _t + 1; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"(((_t < _na) || (_s == 0)) ? (1u << 29) : 0); "
                    f"ctx->gpr[{rd}] = _s; }}")

        if mn in ("neg", "neg."):
            # Full 64-bit negate (the 32-bit truncation zeroed neg of any value
            # with a clear low word, e.g. 0x8000000000000000 -> 0).
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{rd}] = (uint64_t)0 - ctx->gpr[{ra}];"

        if mn == "mulli":
            # PowerISA: RT = low 64 of RA * EXTS(SI) -- a full 64-bit product.
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"ctx->gpr[{rd}] = (uint64_t)((int64_t)ctx->gpr[{ra}] * "
                    f"(int64_t){_imm(ops[2])});")

        if mn in ("mullw", "mullw.", "mullwo", "mullwo."):
            # PowerISA: RT = the FULL 64-bit signed product of the low words.
            # The old (int64_t)(int32_t) truncation replaced the high word with
            # a sign copy, silently breaking every 32x32->64 idiom (fixed-point
            # audio/geometry math; conformance suite 2026-07-02).
            # OE=1 (mullwo[.]): PowerISA_V2.03_Final_Public.pdf p.60/p.36 (XER
            # bit 33) -- OV=1 iff the 32x32 signed product does not fit in 32
            # bits; setting OV=1 also sets SO (sticky) and never clears SO.
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            prod = (f"((int64_t)(int32_t)ctx->gpr[{ra}] * (int64_t)(int32_t)ctx->gpr[{rb}])")
            if mn.startswith("mullwo"):
                return (f"{{ int64_t _p = {prod}; "
                        f"uint32_t _ov = (_p != (int64_t)(int32_t)_p) ? 1u : 0u; "
                        f"ctx->xer = (ctx->xer & ~(1u << 30)) | (_ov << 30); "
                        f"if (_ov) ctx->xer |= (1u << 31); "
                        f"ctx->gpr[{rd}] = (uint64_t)_p; }}")
            return f"ctx->gpr[{rd}] = (uint64_t){prod};"

        # mulhwo/mulhwuo: PowerISA has NO architected OE form for the high-word
        # multiplies (only mullw/mulld get an OE variant -- PowerISA
        # V2.03 Book I sec 3.3.8/3.3.9 tables list mulhw/mulhwu with OE fixed
        # at 0; XO=75/11 with OE=1 is a reserved encoding, most likely
        # data-in-.text rather than a real instruction). ppu_disasm.py's
        # generic xo_arith_3op OE-suffix logic (shared with mullw/mulld,
        # which DO have OE forms) can still emit the mnemonic "mulhwo"/
        # "mulhwuo" for such a reserved encoding, so accept it here and lift
        # it identically to the plain form -- no XER write, since OE=1 is
        # architecturally meaningless for this opcode.
        if mn in ("mulhw", "mulhw.", "mulhwo", "mulhwo."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{rd}] = (int64_t)(int32_t)((int32_t)((int64_t)(int32_t)ctx->gpr[{ra}] * (int64_t)(int32_t)ctx->gpr[{rb}] >> 32));"

        if mn in ("mulhwu", "mulhwu.", "mulhwuo", "mulhwuo."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{rd}] = (int64_t)(int32_t)((int32_t)((uint64_t)(uint32_t)ctx->gpr[{ra}] * (uint64_t)(uint32_t)ctx->gpr[{rb}] >> 32));"

        if mn in ("divw", "divw."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # PPC leaves rD undefined for /0 and INT_MIN/-1; C makes both UB (SIGFPE).
            # Guard -> 0 (matches RPCS3); the . variant still records CR0 from the result.
            return (f"ctx->gpr[{rd}] = (int64_t)(int32_t)(((int32_t)ctx->gpr[{rb}] == 0 || "
                    f"((int32_t)ctx->gpr[{ra}] == (int32_t)0x80000000 && (int32_t)ctx->gpr[{rb}] == -1)) ? 0 : "
                    f"((int32_t)ctx->gpr[{ra}] / (int32_t)ctx->gpr[{rb}]));")

        if mn in ("divwu", "divwu."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"ctx->gpr[{rd}] = (int64_t)(int32_t)((uint32_t)ctx->gpr[{rb}] == 0 ? 0 : "
                    f"((uint32_t)ctx->gpr[{ra}] / (uint32_t)ctx->gpr[{rb}]));")

        # ------- Logical -------
        if mn == "ori":
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] | {_imm(ops[2])};"

        if mn == "oris":
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] | ((uint64_t){_imm(ops[2])} << 16);"

        if mn == "xori":
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] ^ {_imm(ops[2])};"

        if mn == "xoris":
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] ^ ((uint64_t){_imm(ops[2])} << 16);"

        if mn in ("andi.", "andis."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            shift = " << 16" if mn == "andis." else ""
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] & ((uint64_t){_imm(ops[2])}{shift});"

        if mn == "nop":
            return "/* nop */;"

        for log_mn, c_op in [("and", "&"), ("or", "|"), ("xor", "^")]:
            if mn == log_mn or mn == log_mn + ".":
                ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
                return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] {c_op} ctx->gpr[{rb}];"

        if mn in ("nand", "nand."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = ~(ctx->gpr[{rs}] & ctx->gpr[{rb}]);"

        if mn in ("nor", "nor."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = ~(ctx->gpr[{rs}] | ctx->gpr[{rb}]);"

        if mn in ("eqv", "eqv."):
            # XNOR (was an unhandled TODO; conformance suite 2026-07-02).
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = ~(ctx->gpr[{rs}] ^ ctx->gpr[{rb}]);"

        if mn in ("andc", "andc."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] & ~ctx->gpr[{rb}];"

        if mn in ("orc", "orc."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = ctx->gpr[{rs}] | ~ctx->gpr[{rb}];"

        if mn in ("extsb", "extsb."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = (int64_t)(int8_t)ctx->gpr[{rs}];"

        if mn in ("extsh", "extsh."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = (int64_t)(int16_t)ctx->gpr[{rs}];"

        if mn in ("extsw", "extsw."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = (int64_t)(int32_t)ctx->gpr[{rs}];"

        if mn in ("cntlzw", "cntlzw."):
            # cntlzw(0) is DEFINED as 32 on PowerPC; raw __builtin_clz(0) is UB
            # (x86 BSR garbage). Guard like cntlzd below (the 64-bit form had
            # the guard; the 32-bit one didn't -- measured live: Sony's
            # cellSpursQueuePushBody gates its task SendSignal on
            # cntlzw(pending)>>5, and the garbage result silently dropped
            # every SPURS queue wakeup signal).
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"ctx->gpr[{ra}] = ((uint32_t)ctx->gpr[{rs}]) ? "
                    f"__builtin_clz((uint32_t)ctx->gpr[{rs}]) : 32;")

        if mn in ("cntlzd", "cntlzd."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"ctx->gpr[{ra}] = ctx->gpr[{rs}] ? "
                    f"__builtin_clzll(ctx->gpr[{rs}]) : 64;")

        # ------- Shift / Rotate -------
        if mn.startswith("rlwinm"):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            sh, mb, me = ops[2], ops[3], ops[4]
            return (f"ctx->gpr[{ra}] = (int64_t)(int32_t)"
                    f"ppc_rlwinm((uint32_t)ctx->gpr[{rs}], {sh}, {mb}, {me});")

        if mn.startswith("rlwimi"):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            sh, mb, me = ops[2], ops[3], ops[4]
            return (f"ctx->gpr[{ra}] = (int64_t)(int32_t)"
                    f"ppc_rlwimi((uint32_t)ctx->gpr[{ra}], (uint32_t)ctx->gpr[{rs}], {sh}, {mb}, {me});")

        if mn in ("slw", "slw."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # PPC: shift count (bit26 set => >=32) yields 0. Compute in u64 then
            # truncate so counts 32..63 naturally give 0 (a u32<<>=32 is C-UB).
            # Result is ZERO-extended (PowerISA: the mask clears the high word;
            # the old sign-extension put garbage in bits 32-63).
            return f"ctx->gpr[{ra}] = (uint32_t)((uint64_t)(uint32_t)ctx->gpr[{rs}] << (ctx->gpr[{rb}] & 0x3F));"

        if mn in ("srw", "srw."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # Zero-extended like slw (srw of a bit31-set value was sign-extended).
            return f"ctx->gpr[{ra}] = (uint32_t)((uint64_t)(uint32_t)ctx->gpr[{rs}] >> (ctx->gpr[{rb}] & 0x3F));"

        if mn in ("sraw", "sraw."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = ppc_sraw(&ctx->xer, (int32_t)ctx->gpr[{rs}], (int)(ctx->gpr[{rb}] & 0x3F));"

        if mn in ("srawi", "srawi."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = ppc_sraw(&ctx->xer, (int32_t)ctx->gpr[{rs}], {_imm(ops[2])});"

        # ------- 64-bit Rotate / Shift -------
        if mn.startswith("rldicl"):
            # rldicl rA, rS, sh, mb  -- rotate left dword then clear left
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            sh, mb = int(ops[2]), int(ops[3])
            return (f"ctx->gpr[{ra}] = ppc_rldicl(ctx->gpr[{rs}], {sh}, {mb});")

        if mn.startswith("rldicr"):
            # rldicr rA, rS, sh, me  -- rotate left dword then clear right
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            sh, me = int(ops[2]), int(ops[3])
            return (f"ctx->gpr[{ra}] = ppc_rldicr(ctx->gpr[{rs}], {sh}, {me});")

        if mn.startswith("rldic") and not mn.startswith("rldicl") and not mn.startswith("rldicr"):
            # rldic rA, rS, sh, mb
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            sh, mb = int(ops[2]), int(ops[3])
            return (f"ctx->gpr[{ra}] = ppc_rldic(ctx->gpr[{rs}], {sh}, {mb});")

        if mn.startswith("rldcl"):
            # rldcl rA, rS, rB, mb — rotate left dword then clear left (register shift)
            ra, rs, rb_r = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            mb = int(ops[3])
            return (f"ctx->gpr[{ra}] = ppc_rldicl(ctx->gpr[{rs}], (int)(ctx->gpr[{rb_r}] & 63), {mb});")

        if mn.startswith("rldcr"):
            # rldcr rA, rS, rB, me — rotate left dword then clear right (register shift)
            ra, rs, rb_r = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            me = int(ops[3])
            return (f"ctx->gpr[{ra}] = ppc_rldicr(ctx->gpr[{rs}], (int)(ctx->gpr[{rb_r}] & 63), {me});")

        if mn.startswith("rldimi"):
            # rldimi rA, rS, sh, mb  -- rotate left dword then insert mask
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            sh, mb = int(ops[2]), int(ops[3])
            return (f"ctx->gpr[{ra}] = ppc_rldimi(ctx->gpr[{ra}], ctx->gpr[{rs}], {sh}, {mb});")

        # 64-bit shifts
        if mn in ("sld", "sld."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = (ctx->gpr[{rb}] & 64) ? 0 : (ctx->gpr[{rs}] << (ctx->gpr[{rb}] & 63));"

        if mn in ("srd", "srd."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = (ctx->gpr[{rb}] & 64) ? 0 : (ctx->gpr[{rs}] >> (ctx->gpr[{rb}] & 63));"

        if mn in ("srad", "srad."):
            ra, rs, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return f"ctx->gpr[{ra}] = (uint64_t)ppc_srad(&ctx->xer, (int64_t)ctx->gpr[{rs}], (int)(ctx->gpr[{rb}] & 0x7F));"

        if mn in ("sradi", "sradi."):
            ra, rs = _reg_idx(ops[0]), _reg_idx(ops[1])
            return f"ctx->gpr[{ra}] = (uint64_t)ppc_srad(&ctx->xer, (int64_t)ctx->gpr[{rs}], {_imm(ops[2])});"

        # ------- Loads -------
        load_map = {
            "lbz": ("vm_read8", False), "lbzu": ("vm_read8", False),
            "lhz": ("vm_read16", False), "lhzu": ("vm_read16", False),
            "lha": ("vm_read16", True), "lhau": ("vm_read16", True),
            "lwz": ("vm_read32", False), "lwzu": ("vm_read32", False),
            "ld": ("vm_read64", False), "ldu": ("vm_read64", False),
        }
        if mn in load_map:
            helper, signed = load_map[mn]
            rd_i = _reg_idx(ops[0])
            disp, base = _disp_base(ops[1])
            if disp is not None:
                expr = f"{helper}((({base}) ? ctx->gpr[{base}] : 0) + {disp})"
                if signed and "16" in helper:
                    expr = f"(int64_t)(int16_t){expr}"
                line = f"ctx->gpr[{rd_i}] = {expr};"
                # Update forms (ldu/lwzu/lhzu/lbzu/lhau): EA = base+disp, then
                # base = EA. Without this the base pointer never advances, so
                # e.g. the SWAR strcpy (`ldu rX,8(rsrc)`) copies one word forever.
                if mn.endswith("u"):
                    line += f" ctx->gpr[{base}] += {disp};"
                return line
            return f"/* {mn} unhandled operands: {insn.operands} */;"

        # ------- Stores -------
        store_map = {
            "stb": "vm_write8", "stbu": "vm_write8",
            "sth": "vm_write16", "sthu": "vm_write16",
            "stw": "vm_write32", "stwu": "vm_write32",
            "std": "vm_write64", "stdu": "vm_write64",
        }
        if mn in store_map:
            helper = store_map[mn]
            rs_i = _reg_idx(ops[0])
            disp, base = _disp_base(ops[1])
            if disp is not None:
                line = f"{helper}((({base}) ? ctx->gpr[{base}] : 0) + {disp}, ctx->gpr[{rs_i}]);"
                # Handle update forms
                if mn.endswith("u"):
                    line += f" ctx->gpr[{base}] += {disp};"
                return line
            return f"/* {mn} unhandled operands: {insn.operands} */;"

        # ------- Load/Store Multiple Word -------
        # PowerISA_V2.03_Final_Public.pdf p.72 (Book I, section 3.3.5):
        #   lmw RT,D(RA):  b = (RA=0) ? 0 : GPR[RA]; EA = b + EXTS(D);
        #                  for r = RT..31: GPR(r) = 0x0..0 || MEM(EA,4); EA += 4
        #     (n = 32-RT words load into the LOW-order 32 bits of GPRs RT..31,
        #     high-order 32 bits zeroed -- same zero-extending 32-bit guest
        #     load as lwz/vm_read32, just repeated for each register.)
        #   stmw RS,D(RA): same EA; for r = RS..31: MEM(EA,4) = GPR(r)[32:63]; EA += 4
        #     (store the low-order 32 bits of each GPR, same as stw/vm_write32.)
        # rA must not be in the loaded/stored range per the ISA (invalid form
        # otherwise) -- lifted straight per this task's spec, no guard needed.
        if mn == "lmw":
            rd_i = int(_reg_idx(ops[0]))
            disp, base = _disp_base(ops[1])
            if disp is not None:
                stmts = [f"{{ uint64_t _ea = (({base}) ? ctx->gpr[{base}] : 0) + {disp};"]
                for i, r in enumerate(range(rd_i, 32)):
                    off = f" + {i * 4}" if i else ""
                    stmts.append(f" ctx->gpr[{r}] = vm_read32(_ea{off});")
                stmts.append(" }")
                return "".join(stmts)
            return f"/* {mn} unhandled operands: {insn.operands} */;"

        if mn == "stmw":
            rs_i = int(_reg_idx(ops[0]))
            disp, base = _disp_base(ops[1])
            if disp is not None:
                stmts = [f"{{ uint64_t _ea = (({base}) ? ctx->gpr[{base}] : 0) + {disp};"]
                for i, r in enumerate(range(rs_i, 32)):
                    off = f" + {i * 4}" if i else ""
                    stmts.append(f" vm_write32(_ea{off}, (uint32_t)ctx->gpr[{r}]);")
                stmts.append(" }")
                return "".join(stmts)
            return f"/* {mn} unhandled operands: {insn.operands} */;"

        # ------- Indexed Loads -------
        idx_load_map = {
            "lbzx": "vm_read8", "lhzx": "vm_read16", "lwzx": "vm_read32", "ldx": "vm_read64",
            "lhax": "vm_read16", "lwax": "vm_read32",
            "lbzux": "vm_read8", "lhzux": "vm_read16", "lwzux": "vm_read32",
        }
        if mn in idx_load_map:
            helper = idx_load_map[mn]
            rd_i, ra_i, rb_i = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # Indexed update forms (lbzux/lhzux/lwzux): EA = ra+rb, then ra = EA.
            if mn.endswith("ux") and ra_i != "0":
                cast = ""
                if mn in ("lhax", "lwax"):
                    cast = "(int64_t)(int16_t)" if "h" in mn else "(int64_t)(int32_t)"
                return (f"{{ uint64_t ea = ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}]; "
                        f"ctx->gpr[{rd_i}] = {cast}{helper}(ea); ctx->gpr[{ra_i}] = ea; }}")
            ea = f"(ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}])" if ra_i != "0" else f"ctx->gpr[{rb_i}]"
            expr = f"{helper}({ea})"
            if mn in ("lhax", "lwax"):
                cast = "(int16_t)" if "h" in mn else "(int32_t)"
                expr = f"(int64_t){cast}{expr}"
            return f"ctx->gpr[{rd_i}] = {expr};"

        if mn == "lwa":
            rd_i = _reg_idx(ops[0])
            disp, base = _disp_base(ops[1])
            if disp is not None:
                return f"ctx->gpr[{rd_i}] = (int64_t)(int32_t)vm_read32((({base}) ? ctx->gpr[{base}] : 0) + {disp});"

        # ------- Indexed Stores -------
        # audit S2-7: the ux (update) forms were in this plain-store map, so
        # their RA update never happened (the dedicated stwux handler below
        # was shadowed dead code). Book I: store-with-update writes RA <- EA.
        # Zero live sites today (64-bit ABI uses stdux) -- landmine removal.
        idx_store_map = {
            "stbx": "vm_write8", "sthx": "vm_write16", "stwx": "vm_write32", "stdx": "vm_write64",
        }
        if mn in idx_store_map:
            helper = idx_store_map[mn]
            rs_i, ra_i, rb_i = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            ea = f"(ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}])" if ra_i != "0" else f"ctx->gpr[{rb_i}]"
            return f"{helper}({ea}, ctx->gpr[{rs_i}]);"

        idx_store_update_map = {
            "stbux": "vm_write8", "sthux": "vm_write16", "stwux": "vm_write32",
        }
        if mn in idx_store_update_map:
            helper = idx_store_update_map[mn]
            rs_i, ra_i, rb_i = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}]; "
                    f"{helper}(ea, ctx->gpr[{rs_i}]); ctx->gpr[{ra_i}] = ea; }}")

        # ------- Indexed FP Loads/Stores -------
        if mn in ("lfsx", "lfdx"):
            frd, ra_i, rb_i = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            ea = f"(ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}])" if ra_i != "0" else f"ctx->gpr[{rb_i}]"
            if "s" in mn:
                return f"{{ uint32_t tmp = vm_read32({ea}); float ftmp; memcpy(&ftmp, &tmp, 4); ctx->fpr[{frd}] = ftmp; }}"
            else:
                return f"{{ uint64_t tmp = vm_read64({ea}); memcpy(&ctx->fpr[{frd}], &tmp, 8); }}"

        if mn in ("stfsx", "stfdx"):
            frs, ra_i, rb_i = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            ea = f"(ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}])" if ra_i != "0" else f"ctx->gpr[{rb_i}]"
            if "fs" in mn:
                return f"{{ float ftmp = (float)ctx->fpr[{frs}]; uint32_t tmp; memcpy(&tmp, &ftmp, 4); vm_write32({ea}, tmp); }}"
            else:
                return f"{{ uint64_t tmp; memcpy(&tmp, &ctx->fpr[{frs}], 8); vm_write64({ea}, tmp); }}"

        if mn == "stfiwx":
            # Store the low 32 bits of the FPR's raw contents as an integer word.
            frs, ra_i, rb_i = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            ea = f"(ctx->gpr[{ra_i}] + ctx->gpr[{rb_i}])" if ra_i != "0" else f"ctx->gpr[{rb_i}]"
            return f"{{ uint64_t tmp; memcpy(&tmp, &ctx->fpr[{frs}], 8); vm_write32({ea}, (uint32_t)tmp); }}"

        # ------- Compare -------
        if mn in ("cmpwi", "cmpdi"):
            # ops: [crN,] rA, imm  or  rA, imm
            if ops[0].startswith("cr"):
                bf = int(ops[0][2:])
                ra_i = _reg_idx(ops[1])
                imm = _imm(ops[2])
            else:
                bf = 0
                ra_i = _reg_idx(ops[0])
                imm = _imm(ops[1])
            cast = "(int32_t)" if mn == "cmpwi" else "(int64_t)"
            shift = (7 - bf) * 4
            return (f"{{ int64_t a = {cast}ctx->gpr[{ra_i}]; int64_t b = (int64_t){imm}; "
                    f"uint32_t cr_val = (a < b) ? 8 : (a > b) ? 4 : 2; "
                    f"ctx->cr = (ctx->cr & ~(0xFu << {shift})) | (cr_val << {shift}); }}")

        if mn in ("cmplwi", "cmpldi"):
            if ops[0].startswith("cr"):
                bf = int(ops[0][2:])
                ra_i = _reg_idx(ops[1])
                imm = _imm(ops[2])
            else:
                bf = 0
                ra_i = _reg_idx(ops[0])
                imm = _imm(ops[1])
            cast = "(uint32_t)" if mn == "cmplwi" else "(uint64_t)"
            shift = (7 - bf) * 4
            return (f"{{ uint64_t a = {cast}ctx->gpr[{ra_i}]; uint64_t b = (uint64_t){imm}; "
                    f"uint32_t cr_val = (a < b) ? 8 : (a > b) ? 4 : 2; "
                    f"ctx->cr = (ctx->cr & ~(0xFu << {shift})) | (cr_val << {shift}); }}")

        if mn in ("cmpw", "cmpd"):
            if ops[0].startswith("cr"):
                bf = int(ops[0][2:])
                ra_i = _reg_idx(ops[1])
                rb_i = _reg_idx(ops[2])
            else:
                bf = 0
                ra_i = _reg_idx(ops[0])
                rb_i = _reg_idx(ops[1])
            cast = "(int32_t)" if mn == "cmpw" else "(int64_t)"
            shift = (7 - bf) * 4
            return (f"{{ int64_t a = {cast}ctx->gpr[{ra_i}]; int64_t b = {cast}ctx->gpr[{rb_i}]; "
                    f"uint32_t cr_val = (a < b) ? 8 : (a > b) ? 4 : 2; "
                    f"ctx->cr = (ctx->cr & ~(0xFu << {shift})) | (cr_val << {shift}); }}")

        if mn in ("cmplw", "cmpld"):
            if ops[0].startswith("cr"):
                bf = int(ops[0][2:])
                ra_i = _reg_idx(ops[1])
                rb_i = _reg_idx(ops[2])
            else:
                bf = 0
                ra_i = _reg_idx(ops[0])
                rb_i = _reg_idx(ops[1])
            cast = "(uint32_t)" if mn == "cmplw" else "(uint64_t)"
            shift = (7 - bf) * 4
            return (f"{{ uint64_t a = {cast}ctx->gpr[{ra_i}]; uint64_t b = {cast}ctx->gpr[{rb_i}]; "
                    f"uint32_t cr_val = (a < b) ? 8 : (a > b) ? 4 : 2; "
                    f"ctx->cr = (ctx->cr & ~(0xFu << {shift})) | (cr_val << {shift}); }}")

        # ------- Branches -------
        if mn == "blr" or mn == "blrl":
            return "return;"

        if mn == "b":
            target = ops[0]
            try:
                tgt = int(target, 16)
                if func.start_addr <= tgt < func.end_addr:
                    return f"goto loc_{tgt:08X};"
                else:
                    # Use trampoline for cross-fragment branches to avoid
                    # stack-growing recursion when backward branches re-enter
                    # a fragment's prologue (stack allocation).
                    self.branch_targets.add(tgt)
                    return f"{{ g_trampoline_fn = (void(*)(void*))func_{tgt:08X}; return; }}"
            except ValueError:
                return f"goto {target}; /* branch */"

        if mn == "bl":
            target = ops[0]
            try:
                tgt = int(target, 16)
                func.calls.append(tgt)
                self.call_targets.add(tgt)
                return f"func_{tgt:08X}(ctx); DRAIN_TRAMPOLINE(ctx);"
            except ValueError:
                return f"/* bl {target} */;"

        # Conditional return: b<cond>lr [cr]
        # Examples: beqlr, bnelr, bltlr, blelr, bgtlr, bgelr, bdnzlr, bdzlr
        # These branch to LR (i.e. return from the function) when the
        # condition holds. Previously these fell through to the generic
        # branch handler and were emitted as /* blelr cr7 */; no-ops — a
        # huge bug, since any memcpy/memset/str* variant that used an early
        # conditional return silently turned into an infinite tail loop.
        if (mn.endswith("lr") and mn not in ("bl", "blr", "blrl") and
                mn.startswith("b") and
                not mn.startswith("blr")):  # guard against "blr" literal
            cond = self._branch_condition(mn, ops)
            return f"if ({cond}) return;"

        # Conditional indirect branch/call through CTR: b<cond>ctr / b<cond>ctrl.
        # The target is in CTR (resolved at runtime), so always dispatch
        # indirectly — never a fixed target. The "l" (link) forms are CALLS
        # (execution continues after); the non-link forms are tail branches
        # (return after dispatch). NOTE: bcctrl ends in "ctrl", so it must be
        # caught here, before the conditional-branch-with-target handler below
        # (otherwise its operand was mis-parsed as a bogus address).
        if (mn.startswith("b") and mn not in ("bctr", "bctrl") and
                (mn.endswith("ctr") or mn.endswith("ctrl"))):
            cond = self._branch_condition(mn, ops)
            if mn.endswith("ctrl"):   # conditional indirect CALL — continue after
                return f"if ({cond}) {{ ps3_indirect_call(ctx); DRAIN_TRAMPOLINE(ctx); }}"
            return f"if ({cond}) {{ ps3_indirect_call(ctx); DRAIN_TRAMPOLINE(ctx); return; }}"

        # Conditional branches
        if mn.startswith("b") and mn not in ("bl", "b", "blr", "bctr", "bctrl"):
            # Try to extract target from last operand
            target_str = ops[-1] if ops else ""
            try:
                tgt = int(target_str, 16)
                if func.start_addr <= tgt < func.end_addr:
                    cond = self._branch_condition(mn, ops)
                    return f"if ({cond}) goto loc_{tgt:08X};"
                else:
                    cond = self._branch_condition(mn, ops)
                    # Use trampoline for cross-fragment conditional branches
                    self.branch_targets.add(tgt)
                    return f"if ({cond}) {{ g_trampoline_fn = (void(*)(void*))func_{tgt:08X}; return; }}"
            except ValueError:
                return f"/* {mn} {insn.operands} */;"

        if mn == "bctr":
            return "ps3_indirect_call(ctx); return;"

        if mn == "bctrl":
            # Indirect call through CTR register. The CTR value is a GUEST
            # address (or OPD pointer). We dispatch through a hash table
            # that maps guest addresses to host function pointers.
            return "ps3_indirect_call(ctx); DRAIN_TRAMPOLINE(ctx);"

        # ------- SPR -------
        if mn == "mflr":
            rd_i = _reg_idx(ops[0])
            return f"ctx->gpr[{rd_i}] = ctx->lr;"

        if mn == "mtlr":
            rs_i = _reg_idx(ops[0])
            return f"ctx->lr = ctx->gpr[{rs_i}];"

        if mn == "mfctr":
            rd_i = _reg_idx(ops[0])
            return f"ctx->gpr[{rd_i}] = ctx->ctr;"

        if mn == "mtctr":
            rs_i = _reg_idx(ops[0])
            # Mask CTR to 32 bits — PS3 games use 32-bit loop counts.
            # Without masking, sign-extended 64-bit values from addi/rldicr
            # produce CTR values like 0x9FFFFFFFE0000000 causing infinite loops.
            return f"ctx->ctr = (uint32_t)ctx->gpr[{rs_i}];"

        if mn == "mfcr":
            rd_i = _reg_idx(ops[0])
            return f"ctx->gpr[{rd_i}] = ctx->cr;"

        # VRSAVE (audit 2b): exactly one mfspr/mtspr pair in the EBOOT, a
        # save/restore around vector code. Nothing models per-thread VRSAVE;
        # a benign 0-read + discarded write keeps the pair coherent.
        if mn == "mfspr" and ops and ops[-1].upper() == "VRSAVE":
            rd_i = _reg_idx(ops[0])
            return f"ctx->gpr[{rd_i}] = 0; /* VRSAVE unmodeled (audit 2b) */"
        if mn == "mtspr" and ops and ops[0].upper() == "VRSAVE":
            return "/* mtspr VRSAVE: unmodeled (audit 2b) */;"

        if mn == "mtcr":
            return f"ctx->cr = (uint32_t)ctx->gpr[{_reg_idx(ops[-1])}];"
        if mn == "mtcrf":
            # mtcrf CRM, rS -- update ONLY the CR fields whose CRM bit is set
            # (the others are preserved). CR0 = cr bits 28-31 ... CR7 = bits 0-3;
            # CRM 0x80 selects CR0 ... 0x01 selects CR7.
            crm = int(ops[0], 0) if len(ops) > 1 else 0xFF
            mask = 0
            for f in range(8):
                if crm & (0x80 >> f):
                    mask |= 0xF << (28 - 4 * f)
            rS = _reg_idx(ops[-1])
            if mask == 0xFFFFFFFF:
                return f"ctx->cr = (uint32_t)ctx->gpr[{rS}];"
            return (f"ctx->cr = (ctx->cr & 0x{(~mask) & 0xFFFFFFFF:08X}u) | "
                    f"((uint32_t)ctx->gpr[{rS}] & 0x{mask:08X}u);")

        # ------- Syscall -------
        if mn == "sc":
            return "lv2_syscall(ctx);"

        # ------- Floating-point loads/stores -------
        if mn in ("lfs", "lfsu", "lfd", "lfdu"):
            frd = _reg_idx(ops[0])
            disp, base = _disp_base(ops[1])
            if disp is not None:
                if "s" in mn:
                    line = (f"{{ uint32_t tmp = vm_read32((({base}) ? ctx->gpr[{base}] : 0) + {disp}); "
                            f"float ftmp; memcpy(&ftmp, &tmp, 4); ctx->fpr[{frd}] = ftmp; }}")
                else:
                    line = (f"{{ uint64_t tmp = vm_read64((({base}) ? ctx->gpr[{base}] : 0) + {disp}); "
                            f"memcpy(&ctx->fpr[{frd}], &tmp, 8); }}")
                # Update forms (lfsu/lfdu): RA <- EA, same as the integer path.
                if mn.endswith("u"):
                    line += f" ctx->gpr[{base}] += {disp};"
                return line

        if mn in ("stfs", "stfsu", "stfd", "stfdu"):
            frs = _reg_idx(ops[0])
            disp, base = _disp_base(ops[1])
            if disp is not None:
                if "fs" in mn:
                    line = (f"{{ float ftmp = (float)ctx->fpr[{frs}]; uint32_t tmp; "
                            f"memcpy(&tmp, &ftmp, 4); vm_write32((({base}) ? ctx->gpr[{base}] : 0) + {disp}, tmp); }}")
                else:
                    line = (f"{{ uint64_t tmp; memcpy(&tmp, &ctx->fpr[{frs}], 8); "
                            f"vm_write64((({base}) ? ctx->gpr[{base}] : 0) + {disp}, tmp); }}")
                # Update forms (stfsu/stfdu): RA <- EA, same as the integer path.
                if mn.endswith("u"):
                    line += f" ctx->gpr[{base}] += {disp};"
                return line

        # ------- FP arithmetic -------
        fp_binary = {
            "fadd": "+", "fadds": "+", "fsub": "-", "fsubs": "-",
            "fmul": "*", "fmuls": "*", "fdiv": "/", "fdivs": "/",
        }
        mn_base = mn.rstrip(".")
        if mn_base in fp_binary:
            frd = _reg_idx(ops[0])
            fra = _reg_idx(ops[1])
            frb = _reg_idx(ops[2])
            op_c = fp_binary[mn_base]
            # Book I 4.6.5: the single forms (fadds/fsubs/fmuls/fdivs) round
            # the result to single precision; keeping the double intermediate
            # diverges from real HW (breaks exact-compare/convergence idioms
            # and oracle comparability). Audit S2-4 (~24k sites), oracle
            # RPCS3 FADDS = f32(a+b).
            if mn_base.endswith("s"):
                return (f"ctx->fpr[{frd}] = (double)(float)"
                        f"(ctx->fpr[{fra}] {op_c} ctx->fpr[{frb}]);")
            return f"ctx->fpr[{frd}] = ctx->fpr[{fra}] {op_c} ctx->fpr[{frb}];"

        if mn_base in ("fmr",):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            return f"ctx->fpr[{frd}] = ctx->fpr[{frb}];"

        if mn_base in ("fneg",):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            return f"ctx->fpr[{frd}] = -ctx->fpr[{frb}];"

        if mn_base in ("fabs",):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            return f"ctx->fpr[{frd}] = fabs(ctx->fpr[{frb}]);"

        if mn_base in ("fnabs",):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            return f"ctx->fpr[{frd}] = -fabs(ctx->fpr[{frb}]);"

        # S2-4: single fused forms round to single (see fp_binary comment).
        if mn_base in ("fmadd", "fmadds"):
            frd, fra, frc, frb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2]), _reg_idx(ops[3])
            expr = f"ctx->fpr[{fra}] * ctx->fpr[{frc}] + ctx->fpr[{frb}]"
            if mn_base.endswith("s"):
                return f"ctx->fpr[{frd}] = (double)(float)({expr});"
            return f"ctx->fpr[{frd}] = {expr};"

        if mn_base in ("fmsub", "fmsubs"):
            frd, fra, frc, frb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2]), _reg_idx(ops[3])
            expr = f"ctx->fpr[{fra}] * ctx->fpr[{frc}] - ctx->fpr[{frb}]"
            if mn_base.endswith("s"):
                return f"ctx->fpr[{frd}] = (double)(float)({expr});"
            return f"ctx->fpr[{frd}] = {expr};"

        if mn_base in ("fnmadd", "fnmadds"):
            frd, fra, frc, frb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2]), _reg_idx(ops[3])
            expr = f"-(ctx->fpr[{fra}] * ctx->fpr[{frc}] + ctx->fpr[{frb}])"
            if mn_base.endswith("s"):
                return f"ctx->fpr[{frd}] = (double)(float)({expr});"
            return f"ctx->fpr[{frd}] = {expr};"

        if mn_base in ("fnmsub", "fnmsubs"):
            frd, fra, frc, frb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2]), _reg_idx(ops[3])
            expr = f"-(ctx->fpr[{fra}] * ctx->fpr[{frc}] - ctx->fpr[{frb}])"
            if mn_base.endswith("s"):
                return f"ctx->fpr[{frd}] = (double)(float)({expr});"
            return f"ctx->fpr[{frd}] = {expr};"

        if mn_base in ("frsp",):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            return f"ctx->fpr[{frd}] = (float)ctx->fpr[{frb}];"

        # Book I v2.02 4.6.7 float->int: SATURATE (>max => max, <min => min,
        # NaN => min) and fctiw/fctid round per FPSCR[RN] (default nearest-
        # even) where the z forms truncate. The old plain C casts were UB on
        # NaN/inf/overflow (MSVC: 0x80000000 for everything -- positive
        # overflow SIGN-FLIPPED) and fctiw truncated. Audit S2-3 (3,582
        # sites), oracle RPCS3 PPUInterpreter fctiw fixup. Helpers in the
        # preamble (ppu_f2i32/ppu_f2i64; nearbyint = host default rounding =
        # nearest-even, matching the FPSCR default this title never changes).
        if mn_base in ("fctiw", "fctiwz"):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            rn = 0 if mn_base.endswith("z") else 1
            return (f"{{ uint32_t iv = ppu_f2i32(ctx->fpr[{frb}], {rn}); uint64_t tmp; "
                    f"memcpy(&tmp, &ctx->fpr[{frd}], 8); "
                    f"tmp = (tmp & 0xFFFFFFFF00000000ULL) | iv; "
                    f"memcpy(&ctx->fpr[{frd}], &tmp, 8); }}")

        if mn_base in ("fctid", "fctidz"):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            rn = 0 if mn_base.endswith("z") else 1
            return (f"{{ uint64_t iv = ppu_f2i64(ctx->fpr[{frb}], {rn}); "
                    f"memcpy(&ctx->fpr[{frd}], &iv, 8); }}")

        if mn_base in ("fcfid",):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            return (f"{{ int64_t iv; memcpy(&iv, &ctx->fpr[{frb}], 8); "
                    f"ctx->fpr[{frd}] = (double)iv; }}")

        if mn_base == "fsqrt" or mn_base == "fsqrts":
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            if mn_base.endswith("s"):
                return f"ctx->fpr[{frd}] = (double)(float)sqrt(ctx->fpr[{frb}]);"
            return f"ctx->fpr[{frd}] = sqrt(ctx->fpr[{frb}]);"

        if mn_base in ("frsqrte", "frsqrtes"):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            if mn_base == "frsqrtes":
                return f"ctx->fpr[{frd}] = (double)(float)(1.0 / sqrt(ctx->fpr[{frb}]));"
            return f"ctx->fpr[{frd}] = 1.0 / sqrt(ctx->fpr[{frb}]);"

        if mn_base in ("fre", "fres"):
            frd = _reg_idx(ops[0])
            frb = _reg_idx(ops[1])
            if mn_base == "fres":
                return f"ctx->fpr[{frd}] = (double)(float)(1.0 / ctx->fpr[{frb}]);"
            return f"ctx->fpr[{frd}] = 1.0 / ctx->fpr[{frb}];"

        if mn_base == "fsel":
            frd, fra, frc, frb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2]), _reg_idx(ops[3])
            return f"ctx->fpr[{frd}] = (ctx->fpr[{fra}] >= 0.0) ? ctx->fpr[{frc}] : ctx->fpr[{frb}];"

        # ------- FP compare -------
        # Book I v2.02 4.6.8: either operand NaN => c = 0b0001 (FU/unordered).
        # The old emission mapped NaN to EQ(2), so every cror-composed >=/<=
        # after fcmpu took the wrong arm on NaN-poisoned floats (7,975 sites;
        # audit S2-2, oracle RPCS3 ppu_set_fpcc).
        if mn_base == "fcmpu" or mn_base == "fcmpo":
            if ops[0].startswith("cr"):
                bf = int(ops[0][2:])
                fra = _reg_idx(ops[1])
                frb = _reg_idx(ops[2])
            else:
                bf = 0
                fra = _reg_idx(ops[0])
                frb = _reg_idx(ops[1])
            shift = (7 - bf) * 4
            return (f"{{ double a = ctx->fpr[{fra}]; double b = ctx->fpr[{frb}]; "
                    f"uint32_t cr_val = (a != a || b != b) ? 1 : (a < b) ? 8 : (a > b) ? 4 : 2; "
                    f"ctx->cr = (ctx->cr & ~(0xFu << {shift})) | (cr_val << {shift}); }}")

        # ------- 64-bit multiply / divide -------
        # (mullw/mullw. handled earlier with the mullwo/mullwo. group -- that
        # match comes first in this if-chain, so a second `mn == "mullw"`
        # clause here would be dead/unreachable code; T7 fuzz sweep flagged
        # it as shadowed and it has been removed.)
        if mn in ("mulld", "mulld.", "mulldo", "mulldo."):
            # PowerISA_V2.03_Final_Public.pdf p.60/p.36: OE=1 (mulldo[.]) sets
            # OV=1 iff the true 128-bit signed product does not fit in 64 bits
            # (i.e. the high 64 bits are not just the sign-extension of the
            # low 64). Use _mul128/ppc_mulhd (already emitted for mulhd) to
            # get the exact high half without signed-multiply-overflow UB.
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            if mn.startswith("mulldo"):
                return (f"{{ int64_t _a = (int64_t)ctx->gpr[{ra}], _b = (int64_t)ctx->gpr[{rb}]; "
                        f"uint64_t _lo = (uint64_t)_a * (uint64_t)_b; "
                        f"int64_t _hi = ppc_mulhd(_a, _b); "
                        f"uint32_t _ov = (_hi != ((int64_t)_lo >> 63)) ? 1u : 0u; "
                        f"ctx->xer = (ctx->xer & ~(1u << 30)) | (_ov << 30); "
                        f"if (_ov) ctx->xer |= (1u << 31); "
                        f"ctx->gpr[{rd}] = _lo; }}")
            return f"ctx->gpr[{rd}] = (uint64_t)((int64_t)ctx->gpr[{ra}] * (int64_t)ctx->gpr[{rb}]);"

        if mn == "divd":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # Guard /0 and INT64_MIN/-1 (both UB in C) -> 0, as RPCS3 does.
            return (f"ctx->gpr[{rd}] = ((int64_t)ctx->gpr[{rb}] == 0 || "
                    f"((int64_t)ctx->gpr[{ra}] == (int64_t)0x8000000000000000ULL && (int64_t)ctx->gpr[{rb}] == -1)) "
                    f"? 0 : ((int64_t)ctx->gpr[{ra}] / (int64_t)ctx->gpr[{rb}]);")

        if mn == "divdu":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"ctx->gpr[{rd}] = (ctx->gpr[{rb}] != 0) ? "
                    f"ctx->gpr[{ra}] / ctx->gpr[{rb}] : 0;")

        # ------- Add/subtract extended (with carry) -------
        # addeo[.]: PowerISA_V2.03_Final_Public.pdf p.60/p.36 -- OE=1 sets
        # XER[OV] = signed overflow of RA+RB+CA (64-bit), XER[SO] sticky-ORs
        # with OV, matching RPCS3's ADDE (add64_flags + ppu_ov_set: same-sign
        # operands, result sign differs -- rpcs3/Emu/Cell/PPUInterpreter.cpp
        # ADDE/ppu_ov_set, semantics only, no code copied). CA logic unchanged.
        if mn in ("adde", "adde.", "addeo", "addeo."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            ov_block = ""
            if mn.startswith("addeo"):
                ov_block = (
                    f"uint32_t _ov = (((ctx->gpr[{ra}] ^ ctx->gpr[{rb}]) & 0x8000000000000000ULL) == 0 && "
                    f"((ctx->gpr[{ra}] ^ result) & 0x8000000000000000ULL) != 0) ? 1u : 0u; "
                    f"ctx->xer = (ctx->xer & ~(1u << 30)) | (_ov << 30); "
                    f"if (_ov) ctx->xer |= (1u << 31); ")
            return (f"{{ uint64_t ca = (ctx->xer >> 29) & 1; "
                    f"uint64_t result = ctx->gpr[{ra}] + ctx->gpr[{rb}] + ca; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"((result < ctx->gpr[{ra}] || (ca && result == ctx->gpr[{ra}])) ? (1u << 29) : 0); "
                    f"{ov_block}"
                    f"ctx->gpr[{rd}] = result; }}")

        if mn in ("addze", "addze."):
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"{{ uint64_t ca = (ctx->xer >> 29) & 1; "
                    f"uint64_t result = ctx->gpr[{ra}] + ca; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"((result < ctx->gpr[{ra}]) ? (1u << 29) : 0); "
                    f"ctx->gpr[{rd}] = result; }}")

        if mn in ("subfe", "subfe.", "subfeo", "subfeo."):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            # XER[CA] = ADC carry-out of (~RA + RB + ca), per RPCS3 add64_flags.
            # subfeo[.]: PowerISA p.60/p.36 -- OE=1 sets XER[OV] = signed
            # overflow of the same (~RA + RB + ca) sum (operands are ~RA and
            # RB, exactly as SUBFE's own add64_flags call uses them; RPCS3's
            # SUBFE ppu_ov_set: (~RA)[63]==RB[63] && (~RA)[63]!=result[63] --
            # semantics only, no code copied). CA logic unchanged.
            ov_block = ""
            if mn.startswith("subfeo"):
                ov_block = (
                    f"uint32_t _ov = ((a ^ b) & 0x8000000000000000ULL) == 0 && "
                    f"((a ^ result) & 0x8000000000000000ULL) != 0 ? 1u : 0u; "
                    f"ctx->xer = (ctx->xer & ~(1u << 30)) | (_ov << 30); "
                    f"if (_ov) ctx->xer |= (1u << 31); ")
            return (f"{{ uint64_t ca = (ctx->xer >> 29) & 1; "
                    f"uint64_t a = ~ctx->gpr[{ra}], b = ctx->gpr[{rb}]; "
                    f"uint64_t s1 = a + b; uint64_t co = (s1 < a); "
                    f"uint64_t result = s1 + ca; co |= (result < s1); "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | ((uint32_t)co << 29); "
                    f"{ov_block}"
                    f"ctx->gpr[{rd}] = result; }}")

        # ------- addc/subfc (carry arithmetic, carry-out only, no carry-in) -------
        # startswith() captures the . (record) and o (overflow) variants, matching
        # the addme/subfme/subfze family. CA is XER bit 29.
        # addc:  rD = rA + rB; CA = carry out of the add (unsigned wrap => result < rA).
        if mn.startswith("addc"):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t result = ctx->gpr[{ra}] + ctx->gpr[{rb}]; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"((result < ctx->gpr[{ra}]) ? (1u << 29) : 0); "
                    f"ctx->gpr[{rd}] = result; }}")

        # subfc: rD = ~rA + rB + 1 (= rB - rA); CA = carry out of ~rA + rB + 1,
        # which equals 1 iff rB >= rA (unsigned).
        if mn.startswith("subfc"):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t result = ~ctx->gpr[{ra}] + ctx->gpr[{rb}] + 1; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"((ctx->gpr[{rb}] >= ctx->gpr[{ra}]) ? (1u << 29) : 0); "
                    f"ctx->gpr[{rd}] = result; }}")

        # ------- Condition register logical ops -------
        if mn == "cror":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((a | b) << (31 - {bt})); }}")

        if mn == "crand":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((a & b) << (31 - {bt})); }}")

        if mn == "crnand":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((~(a & b) & 1) << (31 - {bt})); }}")

        if mn == "crxor":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((a ^ b) << (31 - {bt})); }}")

        if mn == "crnor":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((~(a | b) & 1) << (31 - {bt})); }}")

        if mn == "creqv":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((~(a ^ b) & 1) << (31 - {bt})); }}")

        if mn == "crorc":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((a | (~b & 1)) << (31 - {bt})); }}")

        if mn == "crandc":
            bt, ba, bb = int(ops[0]), int(ops[1]), int(ops[2])
            return (f"{{ uint32_t a = (ctx->cr >> (31 - {ba})) & 1; "
                    f"uint32_t b = (ctx->cr >> (31 - {bb})) & 1; "
                    f"ctx->cr = (ctx->cr & ~(1u << (31 - {bt}))) | ((a & (~b & 1)) << (31 - {bt})); }}")

        # mcrf — move condition register field
        if mn == "mcrf":
            # mcrf crD, crS — copy 4-bit CR field
            bf = int(ops[0][2:]) if ops[0].startswith("cr") else int(ops[0])
            bfa = int(ops[1][2:]) if ops[1].startswith("cr") else int(ops[1])
            dst_shift = (7 - bf) * 4
            src_shift = (7 - bfa) * 4
            return (f"{{ uint32_t field = (ctx->cr >> {src_shift}) & 0xF; "
                    f"ctx->cr = (ctx->cr & ~(0xFu << {dst_shift})) | (field << {dst_shift}); }}")

        # rlwnm — rotate left word then AND with mask (register shift)
        if mn.startswith("rlwnm"):
            ra = _reg_idx(ops[0])
            rs = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            mb = int(ops[3])
            me = int(ops[4])
            return f"ctx->gpr[{ra}] = ppc_rlwinm((uint32_t)ctx->gpr[{rs}], (int)(ctx->gpr[{rb}] & 31), {mb}, {me});"

        # mffs — move from FPSCR
        if mn == "mffs" or mn == "mffs.":
            frd = _reg_idx(ops[0])
            return f"{{ uint64_t fpscr64 = ctx->fpscr; memcpy(&ctx->fpr[{frd}], &fpscr64, 8); }}"

        # mtfsf — move to FPSCR fields
        if mn == "mtfsf":
            return f"/* mtfsf: FPSCR update — ignored for now */;"

        # FPSCR bit ops (audit S2-10: previously undecoded => invisible
        # `.word`). FPSCR is unmodeled; visible named no-ops.
        if mn.rstrip(".") in ("mtfsb0", "mtfsb1", "mtfsfi"):
            return f"/* {mn} {insn.operands}: FPSCR bit update — FPSCR unmodeled */;"

        # ------- Store/load with update indexed -------
        if mn == "stdux":
            rs, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"vm_write64(ea, ctx->gpr[{rs}]); ctx->gpr[{ra}] = ea; }}")

        # (stwux is handled by idx_store_update_map above -- the old dedicated
        # handler here was shadowed dead code, audit S2-7.)

        if mn == "ldux":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ctx->gpr[{rd}] = vm_read64(ea); ctx->gpr[{ra}] = ea; }}")

        if mn == "lwzux":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ctx->gpr[{rd}] = vm_read32(ea); ctx->gpr[{ra}] = ea; }}")

        # ------- Atomic load/store with reservation -------
        # REAL reservation semantics (2026-07-03, CBEA-audit P1). The old
        # inline emission was an ADDRESS-ONLY per-thread check followed by a
        # PLAIN store: any concurrent writer (another PPU thread, an SPU
        # PUTLLC) between lwarx and stwcx was silently reverted -- a lost-
        # update race that scaled with line traffic. Measured killing Sony's
        # PPU-side SendWorkloadSignal readyCount CAS on the hot SPURS mgmt
        # line (the image-5 taskset was never selected). The runtime helpers
        # (yakuza/shims.cpp ppu_res_*) implement value-verified CAS commits,
        # serialized with the SPU lock-line for coherence-set lines, and set
        # CR0 themselves.
        if mn == "lwarx":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ctx->gpr[{rd}] = ppu_res_lwarx(ctx, ea); }}")

        if mn == "stwcx" or mn == "stwcx.":
            rs, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ppu_res_stwcx(ctx, ea, (uint32_t)ctx->gpr[{rs}]); }}")

        if mn == "ldarx":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ctx->gpr[{rd}] = ppu_res_ldarx(ctx, ea); }}")

        if mn == "stdcx" or mn == "stdcx.":
            rs, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ppu_res_stdcx(ctx, ea, ctx->gpr[{rs}]); }}")

        # ------- Trap (tw) — used for assertions, safe to no-op in recomp -------
        if mn == "tw" or mn == "twi" or mn == "td" or mn == "tdi":
            return f"/* trap: {mn} {insn.operands} — ignored */;"

        # ------- Byte-reverse loads/stores -------
        if mn == "lwbrx":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint32_t raw; memcpy(&raw, vm_base + (uint32_t)ea, 4); "
                    f"ctx->gpr[{rd}] = raw; }}") # NOTE: no bswap — reads in host (LE) order

        if mn == "stwbrx":
            rs, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint32_t raw = (uint32_t)ctx->gpr[{rs}]; "
                    f"memcpy(vm_base + (uint32_t)ea, &raw, 4); }}")

        if mn == "lhbrx":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint16_t raw; memcpy(&raw, vm_base + (uint32_t)ea, 2); "
                    f"ctx->gpr[{rd}] = raw; }}")

        if mn == "ldbrx":
            # Load doubleword byte-reverse: a raw 8-byte copy yields the
            # byte-reversed (little-endian) value on the LE host, same convention
            # as lhbrx above.
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint64_t raw; memcpy(&raw, vm_base + (uint32_t)ea, 8); "
                    f"ctx->gpr[{rd}] = raw; }}")

        if mn == "sthbrx":
            rs, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint16_t raw = (uint16_t)ctx->gpr[{rs}]; "
                    f"memcpy(vm_base + (uint32_t)ea, &raw, 2); }}")

        # ------- Load algebraic -------
        if mn == "lwax":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ctx->gpr[{rd}] = (int64_t)(int32_t)vm_read32(ea); }}")

        if mn == "lhaux":
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"ctx->gpr[{rd}] = (int64_t)(int16_t)vm_read16(ea); ctx->gpr[{ra}] = ea; }}")

        # ------- Move from time base -------
        # THE guest clock. The old emission was a per-call-site static that
        # advanced 16667 ticks PER READ -- not time: every site had a private
        # counter that only moved when polled. Guest timing math (the CRI ADXM
        # VV pacer that schedules the whole media/async-FS stack, throttles,
        # profilers) computed garbage from it; found 2026-07-03 as the root of
        # the shader-build stall (pacer period stretched ~108x, starving the
        # async-file pump). Real semantics: one global monotonic timebase at
        # 79.8 MHz (runtime ppu_timebase_now).
        if mn == "mftb":
            rd = _reg_idx(ops[0])
            return f"ctx->gpr[{rd}] = ppu_timebase_now();"

        if mn == "mftbu":
            rd = _reg_idx(ops[0])
            return f"ctx->gpr[{rd}] = (ppu_timebase_now() >> 32);"

        # ------- Cache/sync ops (safe to no-op) -------
        # dcbz zeros a 128-byte cache line — MUST be implemented, not no-oped!
        # Games use dcbz loops to zero large memory regions. If we no-op dcbz,
        # the loop runs trillions of iterations doing nothing.
        if mn == "dcbz":
            ra = _reg_idx(ops[0])
            rb = _reg_idx(ops[1])
            return (f"{{ uint64_t ea = ((({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]) & ~0x7FULL; "
                    f"memset(vm_base + (uint32_t)ea, 0, 128); }}")

        if mn in ("dcbt", "dcbtst", "dcbf", "dcbst", "dcba", "icbi",
                  "sync", "eieio", "isync", "lwsync", "ptesync"):
            return f"/* {mn}: cache/sync — no-op */;"

        # ------- addme/subfme/subfze (carry arithmetic, 2-op) -------
        if mn.startswith("addme"):
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            # addme = RA + CA - 1 = RA + (~0) + CA; XER[CA] = ADC carry-out.
            return (f"{{ uint64_t ca = (ctx->xer >> 29) & 1; "
                    f"uint64_t a = ctx->gpr[{ra}], b = ~0ULL; "
                    f"uint64_t s1 = a + b; uint64_t co = (s1 < a); "
                    f"uint64_t result = s1 + ca; co |= (result < s1); "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | ((uint32_t)co << 29); "
                    f"ctx->gpr[{rd}] = result; }}")

        if mn.startswith("subfme"):
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            # subfme = ~RA + CA - 1 = ~RA + (~0) + CA; was MISSING the XER[CA] update.
            return (f"{{ uint64_t ca = (ctx->xer >> 29) & 1; "
                    f"uint64_t a = ~ctx->gpr[{ra}], b = ~0ULL; "
                    f"uint64_t s1 = a + b; uint64_t co = (s1 < a); "
                    f"uint64_t result = s1 + ca; co |= (result < s1); "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | ((uint32_t)co << 29); "
                    f"ctx->gpr[{rd}] = result; }}")

        if mn.startswith("subfze"):
            rd, ra = _reg_idx(ops[0]), _reg_idx(ops[1])
            return (f"{{ uint64_t ca = (ctx->xer >> 29) & 1; "
                    f"uint64_t result = ~ctx->gpr[{ra}] + ca; "
                    f"ctx->xer = (ctx->xer & ~(1u << 29)) | "
                    f"((ca && result == 0) ? (1u << 29) : 0); "
                    f"ctx->gpr[{rd}] = result; }}")

        # ------- mulhd / mulhdu (multiply high doubleword) -------
        if mn.startswith("mulhd") and not mn.startswith("mulhdu"):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"ctx->gpr[{rd}] = (uint64_t)ppc_mulhd("
                    f"(int64_t)ctx->gpr[{ra}], (int64_t)ctx->gpr[{rb}]);")

        if mn.startswith("mulhdu"):
            rd, ra, rb = _reg_idx(ops[0]), _reg_idx(ops[1]), _reg_idx(ops[2])
            return (f"ctx->gpr[{rd}] = (uint64_t)ppc_mulhdu("
                    f"ctx->gpr[{ra}], ctx->gpr[{rb}]);")

        # ------- VMX/AltiVec vector load/store -------
        # These are the foundation for all SIMD code. Vector registers
        # are 128-bit (ctx->vr[N], type u128 or uint8_t[16]).
        if mn == "lvx":
            vd = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            return (f"{{ uint64_t ea = ((({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]) & ~0xFULL; "
                    f"memcpy(&ctx->vr[{vd}], vm_base + (uint32_t)ea, 16); }}")

        if mn == "stvx":
            vs = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            return (f"{{ uint64_t ea = ((({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]) & ~0xFULL; "
                    f"memcpy(vm_base + (uint32_t)ea, &ctx->vr[{vs}], 16); }}")

        # Cell unaligned vector loads (CBEA / AltiVec): lvlx loads bytes
        # [EA&15 .. 15] of the aligned quadword left-justified into vD and
        # zero-fills the right; lvrx is the mirror (fills the right end). lvlxl
        # is lvlx with a cache hint (same data). Byte-loop matches the raw-byte
        # convention used by vperm/lvsl.
        if mn == "lvlx" or mn == "lvlxl":
            vd = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint32_t sh = (uint32_t)(ea & 0xF); "
                    f"uint8_t* m = vm_base + (uint32_t)(ea & ~0xFULL); "
                    f"uint8_t* d = (uint8_t*)&ctx->vr[{vd}]; "
                    f"for (int i = 0; i < 16; i++) d[i] = (sh + (uint32_t)i < 16) ? m[sh + i] : 0; }}")

        if mn == "lvrx" or mn == "lvrxl":
            vd = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint32_t sh = (uint32_t)(ea & 0xF); "
                    f"uint8_t* m = vm_base + (uint32_t)(ea & ~0xFULL); "
                    f"uint8_t* d = (uint8_t*)&ctx->vr[{vd}]; "
                    f"for (int i = 0; i < 16; i++) d[i] = ((uint32_t)i >= 16u - sh) ? m[i - (int)(16u - sh)] : 0; }}")

        # Cell unaligned vector STORES -- the mirrors of lvlx/lvrx (audit 5d:
        # previously undecoded => silent NO-OP stores, a memory-corruption
        # class; the classic pair writes one unaligned quadword via
        # stvlx(low part)+stvrx(high part)). stvlx stores vS bytes [0 ..
        # 15-(EA&15)] to [EA .. align_up-1]; stvrx stores the TOP (EA&15)
        # bytes of vS to [EA&~15 .. EA-1]. Raw-byte convention as the loads.
        if mn in ("stvlx", "stvlxl"):
            vs = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint32_t sh = (uint32_t)(ea & 0xF); "
                    f"uint8_t* m = vm_base + (uint32_t)(ea & ~0xFULL); "
                    f"uint8_t* s = (uint8_t*)&ctx->vr[{vs}]; "
                    f"for (uint32_t i = sh; i < 16; i++) m[i] = s[i - sh]; }}")

        if mn in ("stvrx", "stvrxl"):
            vs = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"uint32_t sh = (uint32_t)(ea & 0xF); "
                    f"uint8_t* m = vm_base + (uint32_t)(ea & ~0xFULL); "
                    f"uint8_t* s = (uint8_t*)&ctx->vr[{vs}]; "
                    f"for (uint32_t i = 0; i < sh; i++) m[i] = s[16u - sh + i]; }}")

        if mn == "lvebx" or mn == "lvehx" or mn == "lvewx":
            vd = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            # These load a single element into the vector register.
            # For simplicity, zero the register and load at the element position.
            size = {"lvebx": 1, "lvehx": 2, "lvewx": 4}[mn]
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"memset(&ctx->vr[{vd}], 0, 16); "
                    f"memcpy(&ctx->vr[{vd}], vm_base + (uint32_t)ea, {size}); }}")

        if mn == "stvebx" or mn == "stvehx" or mn == "stvewx":
            vs = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            size = {"stvebx": 1, "stvehx": 2, "stvewx": 4}[mn]
            return (f"{{ uint64_t ea = (({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]; "
                    f"memcpy(vm_base + (uint32_t)ea, &ctx->vr[{vs}], {size}); }}")

        if mn == "lvsl" or mn == "lvsr":
            vd = int(ops[0][1:]) if ops[0].startswith("v") else _reg_idx(ops[0])
            ra = _reg_idx(ops[1])
            rb = _reg_idx(ops[2])
            # lvsl/lvsr generate permutation vectors for unaligned loads.
            # lvsl: for byte offset b, generates {b, b+1, b+2, ..., b+15}
            # lvsr: generates {16-b, 17-b, ..., 31-b}
            if mn == "lvsl":
                return (f"{{ uint8_t b = (uint8_t)(((({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]) & 0xF); "
                        f"for (int i = 0; i < 16; i++) ((uint8_t*)&ctx->vr[{vd}])[i] = b + i; }}")
            else:
                return (f"{{ uint8_t b = (uint8_t)(16 - (((({ra}) ? ctx->gpr[{ra}] : 0) + ctx->gpr[{rb}]) & 0xF)); "
                        f"for (int i = 0; i < 16; i++) ((uint8_t*)&ctx->vr[{vd}])[i] = b + i; }}")

        # VMX permute (vperm) — critical for unaligned loads
        if mn == "vperm":
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            vc = int(ops[3][1:])
            return (f"{{ uint8_t* a = (uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b = (uint8_t*)&ctx->vr[{vb}]; "
                    f"uint8_t* c = (uint8_t*)&ctx->vr[{vc}]; "
                    f"uint8_t* d = (uint8_t*)&ctx->vr[{vd}]; "
                    f"uint8_t tmp[16]; "
                    f"for (int i = 0; i < 16; i++) {{ "
                    f"uint8_t sel = c[i] & 0x1F; "
                    f"tmp[i] = (sel < 16) ? a[sel] : b[sel - 16]; }} "
                    f"memcpy(d, tmp, 16); }}")

        # VMX splat (vspltw, vsplth, vspltb) — duplicate one element across vector
        if mn == "vspltw":
            vd = int(ops[0][1:])
            vb = int(ops[1][1:])
            uimm = int(ops[2]) & 3
            return (f"{{ uint32_t* d = (uint32_t*)&ctx->vr[{vd}]; "
                    f"uint32_t val = ((uint32_t*)&ctx->vr[{vb}])[{uimm}]; "
                    f"d[0] = d[1] = d[2] = d[3] = val; }}")

        if mn == "vxor":
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            return (f"{{ uint64_t* d = (uint64_t*)&ctx->vr[{vd}]; "
                    f"uint64_t* a = (uint64_t*)&ctx->vr[{va}]; "
                    f"uint64_t* b = (uint64_t*)&ctx->vr[{vb}]; "
                    f"d[0] = a[0] ^ b[0]; d[1] = a[1] ^ b[1]; }}")

        if mn == "vor":
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            return (f"{{ uint64_t* d = (uint64_t*)&ctx->vr[{vd}]; "
                    f"uint64_t* a = (uint64_t*)&ctx->vr[{va}]; "
                    f"uint64_t* b = (uint64_t*)&ctx->vr[{vb}]; "
                    f"d[0] = a[0] | b[0]; d[1] = a[1] | b[1]; }}")

        # ------- VMX floating-point arithmetic -------
        if mn == "vmaddfp":
            # Vector Multiply-Add Floating-Point: vD = vA * vC + vB
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])  # Note: vmaddfp operand order is vD, vA, vC, vB
            vc = int(ops[3][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; "
                    f"void* b=&ctx->vr[{vb}]; void* c=&ctx->vr[{vc}]; float r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=vrf(a,i)*vrf(c,i)+vrf(b,i); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        if mn == "vnmsubfp":
            # Vector Negative Multiply-Subtract: vD = -(vA * vC - vB) = vB - vA*vC
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            vc = int(ops[3][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; "
                    f"void* b=&ctx->vr[{vb}]; void* c=&ctx->vr[{vc}]; float r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=vrf(b,i)-vrf(a,i)*vrf(c,i); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        if mn == "vaddfp":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]=vrf(a,i)+vrf(b,i); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        if mn == "vsubfp":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]=vrf(a,i)-vrf(b,i); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        if mn == "vmulfp":
            # Not a real PPC instruction but some disassemblers emit it
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]=vrf(a,i)*vrf(b,i); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        # VMX select (vsel) — bitwise select: vD = (vA & ~vC) | (vB & vC)
        if mn == "vsel":
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            vc = int(ops[3][1:])
            return (f"{{ uint64_t* d=(uint64_t*)&ctx->vr[{vd}]; "
                    f"uint64_t* a=(uint64_t*)&ctx->vr[{va}]; "
                    f"uint64_t* b=(uint64_t*)&ctx->vr[{vb}]; "
                    f"uint64_t* c=(uint64_t*)&ctx->vr[{vc}]; "
                    f"d[0]=(a[0]&~c[0])|(b[0]&c[0]); d[1]=(a[1]&~c[1])|(b[1]&c[1]); }}")

        # VMX FP compare (vcmpeqfp/gefp/gtfp), lane values read big-endian.
        # Dot forms (Rc=1) update CR6 per AltiVec PEM Table 2-3 / vcmpeqfp page:
        # CR6 = all_true || 0 || all_false || 0 (nibble bit LT=8 = all lanes
        # true, EQ=2 = all lanes false), written into CR field 6 (bits 4-7). NaN
        # lanes compare false via C (matching PEM: a NaN operand => result 0).
        # (Ordered together with the S2-1 endianness fix: CR6 needs the correct
        # per-lane values, so it must land in the same pass.)
        vcmpfp_family = {"vcmpeqfp": "==", "vcmpgefp": ">=", "vcmpgtfp": ">"}
        base_fp = mn.rstrip(".")
        if base_fp in vcmpfp_family:
            cmpop = vcmpfp_family[base_fp]
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            body = (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; "
                    f"int t=0; for(int i=0;i<4;i++){{ uint32_t r=(vrf(a,i){cmpop}vrf(b,i))?~0u:0u; "
                    f"vstw(d,i,r); t+=r?1:0; }}")
            if mn.endswith("."):
                body += (" uint32_t c6=(t==4?8u:0u)|(t==0?2u:0u); "
                         "ctx->cr=(ctx->cr & ~(0xFu<<4))|(c6<<4);")
            return body + " }"

        # VMX shift (vsldoi) — shift left double by octet immediate
        if mn == "vsldoi":
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            sh = int(ops[3])  # numeric shift count (disasm emits plain number)
            return (f"{{ uint8_t tmp[32]; "
                    f"memcpy(tmp, &ctx->vr[{va}], 16); memcpy(tmp+16, &ctx->vr[{vb}], 16); "
                    f"memcpy(&ctx->vr[{vd}], tmp + {sh}, 16); }}")

        # VMX integer multiply-accumulate
        if mn == "vmsumshm":
            # vmsumshm vD, vA, vB, vC: multiply s16×s16 pairs, add to s32 accumulator
            vd = int(ops[0][1:])
            va = int(ops[1][1:])
            vb = int(ops[2][1:])
            vc = int(ops[3][1:])
            return (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"void* c=&ctx->vr[{vc}]; void* d=&ctx->vr[{vd}]; int32_t r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=(int32_t)vrw(c,i)"
                    f"+(int32_t)(int16_t)vrh(a,i*2)*(int32_t)(int16_t)vrh(b,i*2)"
                    f"+(int32_t)(int16_t)vrh(a,i*2+1)*(int32_t)(int16_t)vrh(b,i*2+1); "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)r[i]); }}")

        # VMX integer compare (lane values read big-endian; dot forms add CR6
        # per finding 4 -- see the vcmpgt_family tail below for the same math).
        if mn.rstrip(".") == "vcmpequh":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            body = (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; "
                    f"int t=0; for(int i=0;i<8;i++){{ uint16_t r=vrh(a,i)==vrh(b,i)?(uint16_t)~0:0; "
                    f"vsth(d,i,r); t+=r?1:0; }}")
            if mn.endswith("."):
                body += (" uint32_t c6=(t==8?8u:0u)|(t==0?2u:0u); "
                         "ctx->cr=(ctx->cr & ~(0xFu<<4))|(c6<<4);")
            return body + " }"

        if mn.rstrip(".") == "vcmpequw":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            body = (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; "
                    f"int t=0; for(int i=0;i<4;i++){{ uint32_t r=vrw(a,i)==vrw(b,i)?~0u:0u; "
                    f"vstw(d,i,r); t+=r?1:0; }}")
            if mn.endswith("."):
                body += (" uint32_t c6=(t==4?8u:0u)|(t==0?2u:0u); "
                         "ctx->cr=(ctx->cr & ~(0xFu<<4))|(c6<<4);")
            return body + " }"

        # Vector compare greater-than, signed/unsigned, byte/half/word.
        # Unhandled until 2026-07-03 (fell to the TODO catch-all = silently
        # skipped; found by the post-relift TODO audit — the 2026-06-12
        # "instruction layer complete" audit predated the gap-rescue function
        # growth that exposed these). Dot forms set CR6 per AltiVec PEM:
        # bit0 (LT, 8) = all lanes true, bit2 (EQ, 2) = all lanes false.
        # (name, elements, width-in-bytes, signed). Lane VALUES read big-endian
        # (S2-1 fix): byte width needs no swap (vr.b[]), half/word go through the
        # vrh/vrw accessors and back through vsth/vstw. CR6 math (654edfb) kept.
        vcmpgt_family = {
            "vcmpgtsb": (16, 1, 1), "vcmpgtsh": (8, 2, 1), "vcmpgtsw": (4, 4, 1),
            "vcmpgtub": (16, 1, 0), "vcmpgtuh": (8, 2, 0), "vcmpgtuw": (4, 4, 0),
        }
        base_mn = mn.rstrip(".")
        if base_mn in vcmpgt_family:
            n, w, signed = vcmpgt_family[base_mn]
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            if w == 1:
                sty, uty = "int8_t", "uint8_t"
                get_a = f"(({sty})((const uint8_t*)a)[i])" if signed else f"((const uint8_t*)a)[i]"
                get_b = f"(({sty})((const uint8_t*)b)[i])" if signed else f"((const uint8_t*)b)[i]"
                setd = "((uint8_t*)d)[i]=r"
            elif w == 2:
                sty, uty = "int16_t", "uint16_t"
                get_a = f"(({sty})vrh(a,i))" if signed else f"vrh(a,i)"
                get_b = f"(({sty})vrh(b,i))" if signed else f"vrh(b,i)"
                setd = "vsth(d,i,r)"
            else:
                sty, uty = "int32_t", "uint32_t"
                get_a = f"(({sty})vrw(a,i))" if signed else f"vrw(a,i)"
                get_b = f"(({sty})vrw(b,i))" if signed else f"vrw(b,i)"
                setd = "vstw(d,i,r)"
            body = (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; "
                    f"int t=0; for(int i=0;i<{n};i++){{ {uty} r={get_a}>{get_b}?({uty})~({uty})0:0; "
                    f"{setd}; t+=r?1:0; }}")
            if mn.endswith("."):
                body += (f" uint32_t c6=(t=={n}?8u:0u)|(t==0?2u:0u); "
                         f"ctx->cr=(ctx->cr & ~(0xFu<<4))|(c6<<4);")
            return body + " }"

        # ------- VMX integer arithmetic (generated from disasm decode table) -------
        # These are simple per-element operations on vector registers.

        # Integer add/sub (unsigned byte/halfword/word modulo)
        vmx_int_binop = {
            "vaddubm": ("uint8_t",  16, "+"), "vadduhm": ("uint16_t", 8, "+"),
            "vadduwm": ("uint32_t", 4,  "+"),
            "vsububm": ("uint8_t",  16, "-"), "vsubuhm": ("uint16_t", 8, "-"),
            "vsubuwm": ("uint32_t", 4,  "-"),
            "vand":    ("uint64_t", 2,  "&"), "vandc":   None, # special
            "vnor":    None, # special
            "vmaxub":  None, "vmaxuh": None, "vmaxuw": None,
            "vminub":  None, "vminuh": None, "vminuw": None,
            "vmaxsb":  None, "vmaxsh": None, "vmaxsw": None,
            "vminsb":  None, "vminsh": None, "vminsw": None,
        }

        # Byte add/sub and the 64-bit-wide vand are lane-order-agnostic (raw
        # bytes / bitwise) => storage cast is correct. The halfword/word add/sub
        # forms reinterpret multi-byte lanes and must read/write big-endian
        # (S2-1): route them through the vrh/vrw accessors.
        if mn in ("vaddubm", "vsububm", "vand"):
            ty, cnt, op = vmx_int_binop[mn]
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ {ty}* d=({ty}*)&ctx->vr[{vd}]; {ty}* a=({ty}*)&ctx->vr[{va}]; "
                    f"{ty}* b=({ty}*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<{cnt};i++) d[i]=a[i]{op}b[i]; }}")

        if mn in ("vadduhm", "vsubuhm", "vadduwm", "vsubuwm"):
            _, cnt, op = vmx_int_binop[mn]
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            g, s = ("vrh", "vsth") if cnt == 8 else ("vrw", "vstw")
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<{cnt};i++) {s}(d,i,(uint32_t)({g}(a,i){op}{g}(b,i))); }}")

        if mn == "vandc":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ uint64_t* d=(uint64_t*)&ctx->vr[{vd}]; "
                    f"uint64_t* a=(uint64_t*)&ctx->vr[{va}]; "
                    f"uint64_t* b=(uint64_t*)&ctx->vr[{vb}]; "
                    f"d[0]=a[0]&~b[0]; d[1]=a[1]&~b[1]; }}")

        if mn == "vnor":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ uint64_t* d=(uint64_t*)&ctx->vr[{vd}]; "
                    f"uint64_t* a=(uint64_t*)&ctx->vr[{va}]; "
                    f"uint64_t* b=(uint64_t*)&ctx->vr[{vb}]; "
                    f"d[0]=~(a[0]|b[0]); d[1]=~(a[1]|b[1]); }}")

        # Integer min/max. Byte lanes are order-agnostic (raw vr.b[]); the
        # halfword/word forms reinterpret multi-byte lanes and read/write
        # big-endian (S2-1) via the vrh/vrw accessors.
        for prefix in ("vmax", "vmin"):
            for suffix, ty, cnt, signed in [("ub","uint8_t",16,0),("uh","uint16_t",8,0),
                                             ("uw","uint32_t",4,0),("sb","int8_t",16,1),
                                             ("sh","int16_t",8,1),("sw","int32_t",4,1)]:
                iname = prefix + suffix
                if mn == iname:
                    vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
                    cmp = ">" if prefix == "vmax" else "<"
                    if cnt == 16:
                        return (f"{{ {ty}* d=({ty}*)&ctx->vr[{vd}]; {ty}* a=({ty}*)&ctx->vr[{va}]; "
                                f"{ty}* b=({ty}*)&ctx->vr[{vb}]; "
                                f"for(int i=0;i<{cnt};i++) d[i]=a[i]{cmp}b[i]?a[i]:b[i]; }}")
                    g, s = ("vrh", "vsth") if cnt == 8 else ("vrw", "vstw")
                    cast = ty  # int16_t/uint16_t/int32_t/uint32_t
                    return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                            f"for(int i=0;i<{cnt};i++){{ {cast} av=({cast}){g}(a,i); {cast} bv=({cast}){g}(b,i); "
                            f"{s}(d,i,(uint32_t)(av{cmp}bv?av:bv)); }} }}")

        # Float min/max (lane values big-endian, S2-1)
        if mn == "vmaxfp" or mn == "vminfp":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            cmp = ">" if mn == "vmaxfp" else "<"
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++){{ float av=vrf(a,i),bv=vrf(b,i); r[i]=av{cmp}bv?av:bv; }} "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        # Splat immediate (vspltisb/h/w) — splat a 5-bit signed immediate
        if mn in ("vspltisb", "vspltish", "vspltisw"):
            vd = int(ops[0][1:])
            simm = int(ops[1]) if not ops[1].startswith("v") else int(ops[1][1:])
            # Sign extend 5-bit to appropriate type
            if simm > 15: simm -= 32
            if mn == "vspltisb":
                return (f"{{ memset(&ctx->vr[{vd}], (uint8_t)(int8_t){simm}, 16); }}")
            elif mn == "vspltish":
                # each halfword lane = sign-extended imm, stored big-endian (S2-1)
                return (f"{{ void* d=&ctx->vr[{vd}]; "
                        f"for(int i=0;i<8;i++) vsth(d,i,(uint16_t)(int16_t)({simm})); }}")
            else:
                return (f"{{ void* d=&ctx->vr[{vd}]; "
                        f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)(int32_t)({simm})); }}")

        # Merge (vmrghb/h/w, vmrglb/h/w) — interleave elements. Mnemonic layout
        # is vmrg + {h|l}(high/low half) + {b|h|w}(element width). The old code
        # keyed width off `"h" in mn[4:]`, which mis-fired for vmrghw/vmrglw
        # ("hw"/"lw" both contain 'h') => they were merged as HALFWORDS. Element
        # width is the LAST char, high/low is mn[4]. This is a pure element
        # (byte) permutation so no endianness bswap is needed (raw storage copy).
        if mn.startswith("vmrg") and len(mn) >= 6 and mn[5] in "bhw":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            high = (mn[4] == "h")
            ty, cnt = {"b": ("uint8_t", 16), "h": ("uint16_t", 8), "w": ("uint32_t", 4)}[mn[5]]
            half = cnt // 2
            off = 0 if high else half
            return (f"{{ {ty} tmp[{cnt}]; {ty}* a=({ty}*)&ctx->vr[{va}]; {ty}* b=({ty}*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<{half};i++) {{ tmp[i*2]=a[{off}+i]; tmp[i*2+1]=b[{off}+i]; }} "
                    f"memcpy(&ctx->vr[{vd}], tmp, 16); }}")

        # Float reciprocal estimate / reciprocal sqrt estimate (lanes big-endian, S2-1)
        if mn == "vrefp":
            vd, vb = int(ops[0][1:]), int(ops[-1][1:])  # vB = last operand (vmx_vx emits vD, vA, vB)
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]=1.0f/vrf(b,i); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        if mn == "vrsqrtefp":
            vd, vb = int(ops[0][1:]), int(ops[-1][1:])  # vB = last operand (vmx_vx emits vD, vA, vB)
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]=1.0f/sqrtf(vrf(b,i)); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        if mn == "vrfim":  # round to FP integer toward -inf (floor); vrfim is vD,vB
            vd, vb = int(ops[0][1:]), int(ops[-1][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]=floorf(vrf(b,i)); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        # The other three VMX round-to-FP-integer forms (audit 5b; PEM: vrfin
        # = round to nearest, vrfiz = toward zero, vrfip = toward +inf). Same
        # shape as vrfim; decode entries added alongside (xo 0x20A/0x24A/0x28A).
        if mn in ("vrfin", "vrfiz", "vrfip"):
            vd, vb = int(ops[0][1:]), int(ops[-1][1:])
            fn = {"vrfin": "nearbyintf", "vrfiz": "truncf", "vrfip": "ceilf"}[mn]
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]={fn}(vrf(b,i)); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        # 2^x / log2(x) estimates (audit 5a; PEM 6-42/6-43, xo 0x18A/0x1CA;
        # RPCS3 PPUOpcodes.h:274,280). exp2f/log2f exceed the spec's 1/32
        # relative-error bound, conforming.
        if mn in ("vexptefp", "vlogefp"):
            vd, vb = int(ops[0][1:]), int(ops[-1][1:])
            fn = "exp2f" if mn == "vexptefp" else "log2f"
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                    f"float r[4]; for(int i=0;i<4;i++) r[i]={fn}(vrf(b,i)); "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        # Float/int convert (operand form "vD, vB, UIMM" — UIMM is a bare int).
        # Word lanes read/written big-endian (S2-1).
        if mn == "vcfsx" or mn == "vcfux":
            vd, vb = int(ops[0][1:]), int(ops[1][1:])
            uimm = int(ops[2]) if len(ops) > 2 and not ops[2].startswith("v") else 0
            cast = "(int32_t)" if mn == "vcfsx" else ""
            scale = f" / {1 << uimm}.0f" if uimm > 0 else ""
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; float r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=(float)({cast}vrw(b,i)){scale}; "
                    f"for(int i=0;i<4;i++) vstf(d,i,r[i]); }}")

        # PEM vctsxs/vctuxs: SATURATE out-of-range (audit S2-3 class: the
        # plain cast was UB and sign-flipped positive overflow); NaN => 0.
        # Word lanes read/written big-endian (S2-1).
        if mn == "vctsxs" or mn == "vctuxs":
            vd, vb = int(ops[0][1:]), int(ops[1][1:])
            uimm = int(ops[2]) if len(ops) > 2 and not ops[2].startswith("v") else 0
            scale = f" * {1 << uimm}.0f" if uimm > 0 else ""
            if mn == "vctsxs":
                return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                        f"for(int i=0;i<4;i++){{ float v=vrf(b,i){scale}; "
                        f"int32_t r = (v!=v) ? 0 : (v>=2147483647.0f) ? 2147483647 : "
                        f"(v<=-2147483648.0f) ? (-2147483647-1) : (int32_t)v; vstw(d,i,(uint32_t)r); }} }}")
            return (f"{{ void* d=&ctx->vr[{vd}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<4;i++){{ float v=vrf(b,i){scale}; "
                    f"uint32_t r = (v!=v) ? 0u : (v>=4294967295.0f) ? 4294967295u : "
                    f"(v<=0.0f) ? 0u : (uint32_t)v; vstw(d,i,r); }} }}")

        # vcmpbfp[.] — bounds compare. Result bit0 (0x80000000) = a>b out high,
        # bit1 (0x40000000) = a<-b out low; NaN operand => BOTH bits set (PEM,
        # audit 4b). Word lanes big-endian (S2-1). Dot form (finding 4): CR6 bit
        # 26 (nibble EQ=2) = all lanes in bounds (all result lanes==0); bit 24
        # (LT=8) always 0 for vcmpbfp (PEM Table 2-3).
        if mn.rstrip(".") == "vcmpbfp":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            body = (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; "
                    f"int allin=1; for(int i=0;i<4;i++) {{ float av=vrf(a,i),bv=vrf(b,i); uint32_t r=0; "
                    f"if(av!=av || bv!=bv) r=0xC0000000u; else {{ "
                    f"if(av>bv) r|=0x80000000u; if(av<-bv) r|=0x40000000u; }} vstw(d,i,r); if(r) allin=0; }}")
            if mn.endswith("."):
                body += (" uint32_t c6=allin?2u:0u; "
                         "ctx->cr=(ctx->cr & ~(0xFu<<4))|(c6<<4);")
            return body + " }"

        # ------- Additional VMX integer instructions -------
        # Byte compare equal (byte lanes are order-agnostic; dot form adds CR6).
        if mn.rstrip(".") == "vcmpequb":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            body = (f"{{ uint8_t* d=(uint8_t*)&ctx->vr[{vd}]; uint8_t* a=(uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; int t=0; "
                    f"for(int i=0;i<16;i++){{ uint8_t r=a[i]==b[i]?0xFFu:0u; d[i]=r; t+=r?1:0; }}")
            if mn.endswith("."):
                body += (" uint32_t c6=(t==16?8u:0u)|(t==0?2u:0u); "
                         "ctx->cr=(ctx->cr & ~(0xFu<<4))|(c6<<4);")
            return body + " }"

        # Saturating add
        if mn == "vaddsbs":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ int8_t* d=(int8_t*)&ctx->vr[{vd}]; int8_t* a=(int8_t*)&ctx->vr[{va}]; "
                    f"int8_t* b=(int8_t*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<16;i++){{int32_t r=(int32_t)a[i]+(int32_t)b[i]; d[i]=(int8_t)(r>127?127:r<-128?-128:r);}} }}")
        # Halfword/word saturating add reinterpret multi-byte lanes => read/write
        # big-endian (S2-1). (Byte forms vaddsbs/vsububs stay raw vr.b[].)
        if mn == "vadduhs":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<8;i++){{uint32_t r=(uint32_t)vrh(a,i)+(uint32_t)vrh(b,i); vsth(d,i,(uint16_t)(r>65535u?65535u:r));}} }}")
        if mn == "vaddshs":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<8;i++){{int32_t r=(int32_t)(int16_t)vrh(a,i)+(int32_t)(int16_t)vrh(b,i); vsth(d,i,(uint16_t)(int16_t)(r>32767?32767:r<-32768?-32768:r));}} }}")
        if mn == "vadduws":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<4;i++){{uint64_t r=(uint64_t)vrw(a,i)+(uint64_t)vrw(b,i); vstw(d,i,(uint32_t)(r>0xFFFFFFFFu?0xFFFFFFFFu:r));}} }}")

        # Subtract saturate
        if mn == "vsubsws":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<4;i++){{int64_t r=(int64_t)(int32_t)vrw(a,i)-(int64_t)(int32_t)vrw(b,i); vstw(d,i,(uint32_t)(int32_t)(r>0x7FFFFFFFLL?0x7FFFFFFFLL:r<-0x80000000LL?-0x80000000LL:r));}} }}")

        if mn == "vsububs":  # subtract unsigned byte, saturate to [0,255] (byte lanes, no swap)
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ uint8_t* d=(uint8_t*)&ctx->vr[{vd}]; uint8_t* a=(uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<16;i++){{int32_t r=(int32_t)a[i]-(int32_t)b[i]; d[i]=(uint8_t)(r<0?0:r);}} }}")

        if mn == "vsum2sws":
            # AltiVec PEM: d.word1 = SAT_s32(a.w0 + a.w1 + b.w1);
            #              d.word3 = SAT_s32(a.w2 + a.w3 + b.w3); d.word0 = d.word2 = 0.
            # Word index = BE element (= storage index); lane VALUES read/written
            # big-endian (S2-1). temp array so vD may alias vA/vB.
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; "
                    f"int64_t s0=(int64_t)(int32_t)vrw(a,0)+(int32_t)vrw(a,1)+(int32_t)vrw(b,1); "
                    f"int64_t s1=(int64_t)(int32_t)vrw(a,2)+(int32_t)vrw(a,3)+(int32_t)vrw(b,3); "
                    f"int32_t r[4]={{0,0,0,0}}; "
                    f"r[1]=(int32_t)(s0>0x7FFFFFFFLL?0x7FFFFFFFLL:s0<-0x80000000LL?-0x80000000LL:s0); "
                    f"r[3]=(int32_t)(s1>0x7FFFFFFFLL?0x7FFFFFFFLL:s1<-0x80000000LL?-0x80000000LL:s1); "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)r[i]); }}")

        if mn == "vupkhsh":
            # Unpack high signed halfword: sign-extend the high 4 halfwords
            # (BE elements 0-3, storage indices 0-3) to 4 words. Lane values
            # read/written big-endian (S2-1). temp so vD may alias vB.
            vd = int(ops[0][1:]); vb = int(ops[-1][1:])
            return (f"{{ void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; int32_t r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=(int32_t)(int16_t)vrh(b,i); "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)r[i]); }}")

        if mn == "vupklsh":
            # Unpack low signed halfword (AltiVec PEM 6-176, sec Fig 6-147):
            # sign-extend the LOW 4 halfwords (BE elements 4-7, storage
            # indices 4-7) to 4 words in vD (storage indices 0-3), same
            # order. Mirror of vupkhsh over the low half. ops[-1] for vB
            # (not ops[1] -- the operand-audit landmine noted for fsqrt).
            vd = int(ops[0][1:]); vb = int(ops[-1][1:])
            return (f"{{ void* b=&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; int32_t r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=(int32_t)(int16_t)vrh(b,4+i); "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)r[i]); }}")

        if mn == "vupkhsb":
            # Unpack high signed byte (AltiVec PEM 6-172, Fig 6-143):
            # sign-extend the HIGH 8 signed bytes (storage indices 0-7) to
            # 8 signed halfwords in vD (storage indices 0-7), same order.
            # Bytes have no endianness (raw index == storage offset); only
            # the halfword destination needs the BE lane helper (vsth).
            # temp array so vD may alias vB. ops[-1] for vB.
            vd = int(ops[0][1:]); vb = int(ops[-1][1:])
            return (f"{{ uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; int16_t r[8]; "
                    f"for(int i=0;i<8;i++) r[i]=(int16_t)(int8_t)b[i]; "
                    f"for(int i=0;i<8;i++) vsth(d,i,(uint16_t)r[i]); }}")

        if mn == "vupklsb":
            # Unpack low signed byte (AltiVec PEM 6-175, Fig 6-146):
            # sign-extend the LOW 8 signed bytes (storage indices 8-15) to
            # 8 signed halfwords in vD (storage indices 0-7), same order.
            # Mirror of vupkhsb over the low half. ops[-1] for vB.
            vd = int(ops[0][1:]); vb = int(ops[-1][1:])
            return (f"{{ uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; void* d=&ctx->vr[{vd}]; int16_t r[8]; "
                    f"for(int i=0;i<8;i++) r[i]=(int16_t)(int8_t)b[8+i]; "
                    f"for(int i=0;i<8;i++) vsth(d,i,(uint16_t)r[i]); }}")

        # Shifts and rotates
        if mn == "vslb":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ uint8_t* d=(uint8_t*)&ctx->vr[{vd}]; uint8_t* a=(uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<16;i++) d[i]=(uint8_t)(a[i]<<(b[i]&7u)); }}")
        # Word-lane shifts/rotates read/write big-endian (S2-1). Per-element
        # shift count uses the low bits of each vB word (PEM: mod 32).
        if mn == "vslw":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"uint32_t r[4]; for(int i=0;i<4;i++) r[i]=vrw(a,i)<<(vrw(b,i)&31u); "
                    f"for(int i=0;i<4;i++) vstw(d,i,r[i]); }}")
        if mn == "vsrw":  # vector shift right word (logical), per-element count = vB word & 31
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"uint32_t r[4]; for(int i=0;i<4;i++) r[i]=vrw(a,i)>>(vrw(b,i)&31u); "
                    f"for(int i=0;i<4;i++) vstw(d,i,r[i]); }}")
        if mn == "vrlb":  # byte lanes: order-agnostic (raw vr.b[])
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ uint8_t* d=(uint8_t*)&ctx->vr[{vd}]; uint8_t* a=(uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<16;i++){{uint8_t s=b[i]&7u; d[i]=s?((a[i]<<s)|(a[i]>>(8u-s))):a[i];}} }}")
        if mn == "vrlw":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"uint32_t r[4]; for(int i=0;i<4;i++){{uint32_t av=vrw(a,i),s=vrw(b,i)&31u; r[i]=s?((av<<s)|(av>>(32u-s))):av;}} "
                    f"for(int i=0;i<4;i++) vstw(d,i,r[i]); }}")
        if mn == "vsraw":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"uint32_t r[4]; for(int i=0;i<4;i++) r[i]=(uint32_t)((int32_t)vrw(a,i)>>(vrw(b,i)&31u)); "
                    f"for(int i=0;i<4;i++) vstw(d,i,r[i]); }}")

        # (vmaxsw handled above by the generic vmax/vmin loop)

        # Integer multiply (odd/even halfword). Even = BE halfword elements
        # 0,2,4,6; odd = 1,3,5,7 (= storage indices). Result words 0-3. Lane
        # VALUES read/written big-endian (S2-1).
        if mn == "vmulouh" or mn == "vmuleuh":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            off = 1 if mn == "vmulouh" else 0
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)vrh(a,2*i+{off})*(uint32_t)vrh(b,2*i+{off})); }}")
        if mn == "vmulosh" or mn == "vmulesh":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            off = 1 if mn == "vmulosh" else 0
            return (f"{{ void* d=&ctx->vr[{vd}]; void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)((int32_t)(int16_t)vrh(a,2*i+{off})*(int32_t)(int16_t)vrh(b,2*i+{off}))); }}")

        # Average unsigned byte
        if mn == "vavgub":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ uint8_t* d=(uint8_t*)&ctx->vr[{vd}]; uint8_t* a=(uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; "
                    f"for(int i=0;i<16;i++) d[i]=(uint8_t)(((unsigned)a[i]+(unsigned)b[i]+1u)>>1); }}")

        # Splat byte (vspltb vD, vB, UIMM — UIMM encoded in vA field)
        # Splat byte/halfword (operand form "vD, vB, UIMM" — UIMM is a bare int,
        # matching the disassembler's vspltb/h/w emission and the vspltw handler).
        if mn == "vspltb":
            vd = int(ops[0][1:]); vb = int(ops[1][1:]); uimm = int(ops[2]) & 15
            return (f"{{ uint8_t* d=(uint8_t*)&ctx->vr[{vd}]; uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; "
                    f"uint8_t v=b[{uimm}]; for(int i=0;i<16;i++) d[i]=v; }}")

        if mn == "vsplth":
            vd = int(ops[0][1:]); vb = int(ops[1][1:]); uimm = int(ops[2]) & 7
            return (f"{{ uint16_t* d=(uint16_t*)&ctx->vr[{vd}]; uint16_t* b=(uint16_t*)&ctx->vr[{vb}]; "
                    f"uint16_t v=b[{uimm}]; for(int i=0;i<8;i++) d[i]=v; }}")

        # Pack signed halfword signed saturate. Source halfword VALUES read
        # big-endian (S2-1); the result is written as single bytes (no swap) to
        # BE byte positions 0-15 (a's lanes -> 0-7, b's -> 8-15). temp so vD may
        # alias vA/vB.
        if mn == "vpkshss":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; uint8_t r[16]; "
                    f"for(int i=0;i<8;i++){{int32_t v=(int16_t)vrh(a,i); r[i]=(uint8_t)(int8_t)(v>127?127:v<-128?-128:v);}} "
                    f"for(int i=0;i<8;i++){{int32_t v=(int16_t)vrh(b,i); r[8+i]=(uint8_t)(int8_t)(v>127?127:v<-128?-128:v);}} "
                    f"memcpy(&ctx->vr[{vd}], r, 16); }}")

        # Pack signed halfword UNSIGNED saturate (audit S2-6; PEM: clamp each
        # signed halfword to [0,255]). Same lane layout as vpkshss above.
        if mn == "vpkshus":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            return (f"{{ void* a=&ctx->vr[{va}]; void* b=&ctx->vr[{vb}]; uint8_t r[16]; "
                    f"for(int i=0;i<8;i++){{int32_t v=(int16_t)vrh(a,i); r[i]=(uint8_t)(v>255?255:v<0?0:v);}} "
                    f"for(int i=0;i<8;i++){{int32_t v=(int16_t)vrh(b,i); r[8+i]=(uint8_t)(v>255?255:v<0?0:v);}} "
                    f"memcpy(&ctx->vr[{vd}], r, 16); }}")

        # Even/odd unsigned-byte multiplies (audit S2-6; PEM vmuleub/vmuloub).
        # Source BYTES are order-agnostic (raw vr.b[]): even = byte elements
        # 0,2,4,... (storage indices), odd = 1,3,5,...; the uint16 result lanes
        # (0-7) are written big-endian (S2-1). temp so vD may alias vA/vB.
        if mn == "vmuleub" or mn == "vmuloub":
            vd, va, vb = int(ops[0][1:]), int(ops[1][1:]), int(ops[2][1:])
            off = 0 if mn == "vmuleub" else 1
            return (f"{{ void* d=&ctx->vr[{vd}]; uint8_t* a=(uint8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* b=(uint8_t*)&ctx->vr[{vb}]; uint16_t r[8]; "
                    f"for(int i=0;i<8;i++) r[i]=(uint16_t)a[2*i+{off}]*(uint16_t)b[2*i+{off}]; "
                    f"for(int i=0;i<8;i++) vsth(d,i,r[i]); }}")

        # vmsummbm (VA-form, 4 operands). Source BYTES order-agnostic; the s32
        # accumulator vC and s32 result vD read/written big-endian (S2-1). temp
        # so vD may alias vC.
        if mn == "vmsummbm":
            vd = int(ops[0][1:]); va = int(ops[1][1:]); vb = int(ops[2][1:]); vc = int(ops[3][1:])
            return (f"{{ void* d=&ctx->vr[{vd}]; int8_t* a=(int8_t*)&ctx->vr[{va}]; "
                    f"uint8_t* ub=(uint8_t*)&ctx->vr[{vb}]; void* c=&ctx->vr[{vc}]; int32_t r[4]; "
                    f"for(int i=0;i<4;i++) r[i]=(int32_t)a[4*i]*(int32_t)ub[4*i]+(int32_t)a[4*i+1]*(int32_t)ub[4*i+1]"
                    f"+(int32_t)a[4*i+2]*(int32_t)ub[4*i+2]+(int32_t)a[4*i+3]*(int32_t)ub[4*i+3]+(int32_t)vrw(c,i); "
                    f"for(int i=0;i<4;i++) vstw(d,i,(uint32_t)r[i]); }}")

        # Merge high halfword / low halfword (vmrghh, vmrglh — already in vmrg* handler above)

        # ------- Default: emit as comment -------
        return f"/* TODO: {mn} {insn.operands} */;"

    def _branch_condition(self, mnemonic: str, ops: list[str]) -> str:
        """Generate a C condition expression for a conditional branch."""
        mn = mnemonic.rstrip("la")  # strip link and absolute bits
        if mn.startswith("b"):
            mn = mn[1:]  # strip leading 'b'

        # strip trailing 'lr', 'ctr'
        mn = mn.replace("lr", "").replace("ctr", "")

        cr_field = 0
        # Check if first operand is a CR field
        if ops and ops[0].startswith("cr"):
            try:
                cr_field = int(ops[0][2:])
            except ValueError:
                pass

        shift = (7 - cr_field) * 4

        cond_map = {
            "eq": f"((ctx->cr >> {shift}) & 2)",
            "ne": f"(!((ctx->cr >> {shift}) & 2))",
            "lt": f"((ctx->cr >> {shift}) & 8)",
            "ge": f"(!((ctx->cr >> {shift}) & 8))",
            "gt": f"((ctx->cr >> {shift}) & 4)",
            "le": f"(!((ctx->cr >> {shift}) & 4))",
            "so": f"((ctx->cr >> {shift}) & 1)",
            "ns": f"(!((ctx->cr >> {shift}) & 1))",
            "dnz": "((ctx->ctr = (uint32_t)(ctx->ctr - 1)) != 0)",
            "dz": "((ctx->ctr = (uint32_t)(ctx->ctr - 1)) == 0)",
        }

        if mn in cond_map:
            return cond_map[mn]

        # Combined CTR+CR forms (audit S2-8: bdnzeq/bdnzne/... previously fell
        # to the always-taken `1` WITHOUT decrementing CTR). Book I 2.4.1 BO:
        # decrement CTR, branch if (ctr-cond) AND (cr-cond). Census shows all
        # current in-range hits are data, so this is landmine removal.
        for ctr_mn, ctr_cond in (("dnz", cond_map["dnz"]), ("dz", cond_map["dz"])):
            if mn.startswith(ctr_mn) and mn[len(ctr_mn):] in cond_map:
                cr_cond = cond_map[mn[len(ctr_mn):]]
                return f"({ctr_cond} && {cr_cond})"

        return f"/* cond: {mnemonic} */ 1"

